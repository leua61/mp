(function(){
  const {n,op,R,Q,C,add}=EX;

  const multi=C('net_train_multi','倍数网络',['技巧'],[
    Q('net_multi_diff_origin','差值等于原项倍数',['技巧'],()=>{
      const k=n(2,4), a=n(2,6); const arr=[a,a+k*a]; const diff=k*a;
      const ans=`${diff} 是 ${a} 的 ${k} 倍`;
      return R(
        `构造网络观察：原项 ${a}，对应差值 ${diff}。它们关系是什么？`, ans,
        op(ans,[`${diff} 是 ${a} 的 ${k+1} 倍`,`${a} 是 ${diff} 的 ${k} 倍`,'没有关系']),
        '看差值与原数列同位置项。',
        `${diff}/${a}=${k}，所以差值是原项的 ${k} 倍。`
      );
    }),
    Q('net_multi_next','反推下一差值',['技巧'],()=>{
      const x=n(12,30), k=n(2,4), last=n(40,80); const d=x*k; const ans=String(last+d);
      return R(
        `若网络规律为“下一差值 = 原项 × ${k}”，当前原项 ${x}，当前末项 ${last}，下一项是多少？`, ans,
        op(ans,[String(last+x),String(last+k),String(last+d+k)]),
        '先求差值，再加回末项。',
        `下一差值=${x}×${k}=${d}，下一项=${last}+${d}=${ans}。`
      );
    })
  ],'sequence');

  const equal=C('net_train_equal','相等网络',['技巧'],[
    Q('net_equal_relation','差值相等',['技巧'],()=>R(
      '间隔差得到 7、2、1，其中 2、1 分别与原数列后续项连续相等。能否尝试构造相等网络？','能',
      ['能','不能','必须全部从第一项相等','只能看倍数'],
      '有效网线不一定从第一项开始。',
      '只要从某点开始连续不中断，就可用相等关系推出后续。'
    )),
    Q('net_broken_line','中间断线',['运用'],()=>R(
      '若差值与原数列的关系是“相等、断开、相等”，这条网络能强行成立吗？','不能',
      ['不能','能','只要选项有就能','看心情'],
      '网络要求连续。',
      '中间断线说明规律不稳定，不能强认。'
    ))
  ],'all');

  const square=C('net_train_square','平方网络',['技巧'],[
    C('net_deep_square','平方尾数路线',['技巧'],[
      Q('net_square_relation','原项等于差值平方',['技巧'],()=>R(
        '若原数列中 4、9、25 分别对应差值 2、3、5，可构造什么关系？','原项=差值平方',
        ['原项=差值平方','原项=差值+1','差值=原项平方','没有关系'],
        '4=2²，9=3²，25=5²。',
        '差值与原项形成平方网络，可据此推出下一项。'
      )),
      Q('net_square_tail','尾数选项',['技巧'],()=>R(
        '若推出答案为 16²，但忘记 16² 精确值，选项尾数只有一个是 6，能否用尾数选？','能',
        ['能','不能','只能精算','必须放弃'],
        '16² 尾数由 6×6 决定。',
        '个位 6 的平方尾数为 6，可用尾数快速锁定选项。'
      ))
    ],'sequence')
  ],'all');

  add(C('network_training','构造网络变量训练｜连续关系',['技巧'],[multi,equal,square],'all'));
})();
