(function(){
  const {n,op,R,Q,C,add,fmt}=DA3;
  const formula=Q('base_formula','基期公式',['基础'],()=>{
    const ans='基期 = 现期 ÷ (1+增长率)';
    return R('已知现期和“现期相对于基期”的增长率，求过去的量，应使用哪个公式？',ans,op(ans,['现期 = 基期 ÷ (1+增长率)','增长量 = 现期 + 基期','增长率 = 现期 + 基期 - 1']),'过去的量是基期。','由 基期×(1+r)=现期 得：基期=现期÷(1+r)。');
  });
  const current=Q('current_formula','现期公式',['基础'],()=>{
    const ans='现期 = 基期 × (1+增长率)';
    return R('已知过去的量和增长率，求现在的量，应使用哪个公式？',ans,op(ans,['基期 = 现期 × (1+增长率)','现期 = 基期 ÷ (1+增长率)','增长率 = 现期 ÷ 增长量']),'基期向现期推进，要乘。','现期比基期增长 r，因此现期=基期×(1+r)。');
  });
  const direct=Q('base_train_direct','一年基期变量',['技巧'],()=>{
    const cur=n(800,2600), r=n(8,35);
    const ans=`${cur} ÷ (1+${r}%)`;
    return R(`2022年某产量为 ${cur} 万吨，比2021年增长 ${r}%。求2021年产量，正确列式是？`,ans,op(ans,[`${cur} × (1+${r}%)`,`${cur} ÷ ${r}%`,`${cur} × ${r}%`]),'2021年是过去，求基期。',`2022年是现期，2021年是基期，所以用现期÷(1+r)：${ans}。`);
  });
  const yearTrap=Q('base_train_year_trap','去年前年陷阱变量',['运用'],()=>{
    const cur=n(900,3200), r1=n(6,18), r2=n(20,45);
    const ans=`${cur} ÷ (1+${r2}%)`;
    return R(`2022年产量为 ${cur}，比去年增长 ${r1}%，比前年增长 ${r2}%。求2020年产量，列式是？`,ans,op(ans,[`${cur} ÷ (1+${r1}%)`,`${cur} × (1+${r2}%)`,`${cur} ÷ (1+${r1}%+${r2}%)`]),'求2020年，就要找2022相对2020的增长率。',`前年是2020年。题干直接给出“2022比前年增长${r2}%”，所以用 ${ans}。${r1}% 是求2021年时才用。`);
  });
  const chain=Q('base_train_chain','连续基期变量',['技巧'],()=>{
    const cur=n(1000,5000), r10=n(20,60), delta=n(5,15); const r09=r10-delta;
    const ans=`${cur} ÷ (1+${r10}%) ÷ (1+${r09}%)`;
    return R(`2010年某值为 ${cur}，2010年增长率 ${r10}%，增幅同比提高 ${delta} 个百分点。求2008年值，列式是？`,ans,op(ans,[`${cur} ÷ (1+${r09}%)`,`${cur} ÷ (1+${r10}%+${r09}%)`,`${cur} × (1+${r10}%) × (1+${r09}%)`]),'先求2009，再求2008。',`2010增长率属于2010年，即2010相对2009；2009增长率=${r10}%-${delta}个百分点=${r09}%。所以2008=${ans}。`);
  });
  const train=C('base_variable','变量训练',['技巧'],[direct,yearTrap,chain],'all');
  add(C('base_current','基期与现期｜公式、时间与增长率匹配',['基础'],[formula,current,train],'sequence'));
})();
