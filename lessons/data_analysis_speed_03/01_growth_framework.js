(function(){
  const {R,Q,C,add,op}=DA3;
  const map=Q('framework_three_blocks','三大块识别',['基础'],()=>{
    const ans='增长、比重、平均数';
    return R('资料分析的大方向，本节课先放进哪三大块框架里理解？',ans,op(ans,['增长、利润、工程','速度、浓度、行程','比重、概率、排列组合']),'先建立课程地图。','资料分析的大方向分为增长、比重、平均数；本节重点展开增长，同时提醒后续还会讲比重和平均数。');
  });
  const flow=Q('framework_process','做题流程',['基础'],()=>{
    const ans='识别题型 → 列公式 → 带数据 → 结合估算和选项';
    return R('本课处理资料分析题的主流程是？',ans,op(ans,['先精算到底 → 再读题','只背公式，不看选项','先看材料全文 → 再随便代数']),'公式不是终点，选项意识也要进入流程。','先根据问法识别增长、比重或平均数；再列式带数；最后结合估算、误差和选项范围决定算到哪里。');
  });
  const unit=Q('framework_unit','单位识别',['基础'],()=>{
    const ans='带单位多为增长量，百分号多为增长率';
    return R('一道题问“比上年增长多少”，选项全是“亿元/万吨/万辆”。这通常说明它问的是？',ans,op(ans,['一定是增长率','一定是百分点','一定是同比时间']),'看选项单位。','增长量是绝对量，通常带单位；增长率是相对量，通常写成百分号。');
  });
  add(C('growth_framework','增长框架｜题型、公式与选项',['基础'],[map,flow,unit],'sequence'));
})();
