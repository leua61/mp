(function(){
  const {n,op,R,Q,C,add}=DA3;
  const rateBelong=Q('time_rate_belong','增长率归属',['基础'],()=>{
    const ans='2010年的增长率，表示2010年相对于2009年';
    return R('“2010年比2009年增长55.6%”这句话可以简写为？',ans,op(ans,['2009年的增长率，表示2009年相对于2010年','2009年的增长率，表示2009年相对于2008年','2010和2009共同的增长率']),'增长率归属于后面的那个年份。','“2010年增长率55.6%”就是2010年相对于2009年的增长率；“2009年增长率”才是2009年相对于2008年。');
  });
  const increase=Q('time_increase_meaning','增幅同比',['基础'],()=>{
    const ans='增幅就是增长率，同比提高是与上一同比期的增长率做差';
    return R('“增幅同比提高20个百分点”中的“增幅”应理解为？',ans,op(ans,['增长量','现期量','基期量']),'增幅不是增长量。','增幅=增长率。同比提高20个百分点，表示本期增长率比同比期增长率高20个百分点。');
  });
  const month=Q('time_month','年月口径变量',['技巧'],()=>{
    const y=n(2020,2025), m=n(2,12);
    const ans=`同比：${y-1}年${m}月；环比：${y}年${m-1}月`;
    return R(`${y}年${m}月的同比和环比分别是？`,ans,op(ans,[`同比：${y}年${m-1}月；环比：${y-1}年${m}月`,`同比：${y-1}年${m-1}月；环比：${y}年${m-1}月`,`同比：${y-1}年${m}月；环比：${y-1}年${m-1}月`]),'只有年月时，常见说法才是同比上一年、环比上个月。',`年月中最大单位是年，所以同比=${y-1}年${m}月；最小单位是月，所以环比=${y}年${m-1}月。`);
  });
  const ymd=Q('time_y_m_d','年月日口径变量',['技巧'],()=>{
    const y=n(2020,2025), m=n(2,11), d=n(2,27);
    const ans=`同比：${y-1}年${m}月${d}日；环比：${y}年${m}月${d-1}日`;
    return R(`${y}年${m}月${d}日的同比时间和环比时间分别是？`,ans,op(ans,[`同比：${y-1}年${m}月${d}日；环比：${y}年${m-1}月${d}日`,`同比：${y}年${m-1}月${d}日；环比：${y}年${m}月${d-1}日`,`同比：${y-1}年${m-1}月${d}日；环比：${y}年${m}月${d-1}日`]),'同比看最大时间单位，环比看最小时间单位。',`年月日中最大单位是年，所以同比是上一年同月同日；最小单位是日，所以环比是前一日。`);
  });
  const quarter=Q('time_quarter','季度口径变量',['运用'],()=>{
    const y=n(2020,2025);
    const ans=`同比：${y-1}年第一季度；环比：${y-1}年第四季度`;
    return R(`${y}年第一季度的同比时间和环比时间分别是？`,ans,op(ans,[`同比：${y-1}年第一季度；环比：${y}年第二季度`,`同比：${y}年上一季度；环比：${y-1}年第一季度`,`同比：${y-1}年第四季度；环比：${y-1}年第一季度`]),'年+季度时，环比是上一季度。',`同比看最大单位年：${y-1}年第一季度；环比看最小单位季度：${y}年第一季度的上一季度是${y-1}年第四季度。`);
  });
  const train=C('time_variable','变量时间训练',['技巧'],[month,ymd,quarter],'all');
  add(C('time_terms','同比环比与增幅｜时间口径和增长率归属',['基础'],[rateBelong,increase,train],'sequence'));
})();
