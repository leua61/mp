(function(){
  const {n,op,R,Q,C,pct,add}=EX;

  const a=Q('avg_definition','平均数定义',['基础'],()=>R(
    '资料分析中“平均数”的基本形式是什么？',
    '总量 ÷ 份数',
    ['总量 ÷ 份数','部分量 ÷ 整体量','增长量 ÷ 增长率','现期 ÷ 基期'],
    '平均、均、每，先找被平均的总量和份数。',
    '平均数不是比重；它是一个总量被分成若干份后的每份水平，也就是总量除以份数。'
  ));

  const a2=Q('avg_keywords','平均数关键词',['基础'],()=>R(
    '看到“平均、均、每户、每家、亩产量、平均功率”时，优先识别为什么题型？',
    '平均数：总量 ÷ 份数',
    ['平均数：总量 ÷ 份数','比重：部分 ÷ 整体','增长率：增长量 ÷ 基期','概率：满足 ÷ 总数'],
    '这些词都在问每一份的水平。',
    '平均功率=总功率/销量，亩产量=总产量/面积，每户费用=总费用/户数。'
  ));



  const a3=Q('avg_present_no_growth','现期平均数不加修正',['基础'],()=>R(
    '材料和问法都是同一年，求“平均每家/平均每月”时，是否需要乘增长率修正项？',
    '不需要，直接总量÷份数',
    ['不需要，直接总量÷份数','必须乘(1+R份)/(1+R总)','必须乘(1+R总)/(1+R份)','只看增长率'],
    '同年就是现期平均数。',
    '只有问过去的平均数才用基期平均数修正项；问材料同年，直接按口径列出现期总量/现期份数。'
  ));

  const b=Q('avg_unit_match','单位匹配变量',['技巧'],()=>{
    const fee=n(36,96), users=n(12,48), months=[3,6,12][n(0,2)];
    const ans=`总费用 ÷ 用户数 ÷ ${months}`;
    return R(
      `某地${months}个月有线网络总费用${fee}亿元，用户${users}万户。求平均每户每月费用，应如何列式？`,
      ans,
      op(ans,[`总费用 ÷ ${months}`,`总费用 ÷ 用户数`,`用户数 ÷ 总费用 ÷ ${months}`]),
      '题目同时包含“每户/用户”和“每月”。',
      `先除以用户数得到每户${months}个月费用，再除以${months}得到每户每月费用。`
    );
  });



  const b2=Q('avg_unit_conversion','平均数单位换算',['运用'],()=>{
    const fee=n(20,96), users=n(10,60), months=[3,6,12][n(0,2)];
    const val=fee*10000/users/months;
    const ans=`约${val.toFixed(1)}元`;
    return R(
      `某地${months}个月有线网络总费用${fee}亿元，用户${users}万户。平均每户每月费用约为多少元？`,
      ans,
      op(ans,[`约${(fee/users/months).toFixed(1)}元`,`约${(fee*10000/users).toFixed(1)}元`,`约${(fee*10000/months).toFixed(1)}元`]),
      '先列总费用÷用户数÷月份，再处理“亿元/万户”的单位换算。',
      `1亿元÷1万户=10000元/户，所以平均每户每月=${fee}×10000÷${users}÷${months}=${val.toFixed(1)}元。原课在有线电视费用题中专门提醒过“单位要换算”。`
    );
  });



  const b3=Q('avg_present_numeric','现期平均数数值',['运用'],()=>{
    const total=n(1200,3600), count=n(300,900);
    const val=total/count;
    const ans=`约${val.toFixed(1)}`;
    return R(
      `某项目现期直接费用${total}万元，资助项目${count}个。平均资助强度约为多少万元/个？`,
      ans,
      op(ans,[`约${(val+0.8).toFixed(1)}`,`约${(val-0.8).toFixed(1)}`,`约${(count/total).toFixed(1)}`]),
      '现期平均数直接总量÷份数。',
      `平均资助强度=${total}÷${count}=${val.toFixed(1)}万元/个。原课在“平均资助强度”处强调先找费用和项目数，再用首位/接近度判断。`
    );
  });

  const b4=Q('avg_multiple_compare','平均数倍数比较',['运用'],()=>{
    const aTotal=n(1800,4200), aCount=n(200,700), bTotal=n(900,2600), bCount=n(300,900);
    const val=(aTotal/aCount)/(bTotal/bCount);
    const ans=`约${val.toFixed(1)}倍`;
    return R(
      `甲类总营业收入${aTotal}万元、${aCount}家；乙类总营业收入${bTotal}万元、${bCount}家。甲类平均营业收入约是乙类的多少倍？`,
      ans,
      op(ans,[`约${(val+1).toFixed(1)}倍`,`约${Math.max(0.1,val-1).toFixed(1)}倍`,`约${(aTotal/bTotal).toFixed(1)}倍`]),
      '先分别求平均，再作倍数比较。',
      `五星级和四星级饭店这类题就是平均数倍数比较。甲平均=${aTotal}/${aCount}，乙平均=${bTotal}/${bCount}；倍数=(${aTotal}/${aCount})÷(${bTotal}/${bCount})≈${val.toFixed(1)}。原课五星/四星饭店题就是平均数之后再比较倍数。`
    );
  });

  const c=Q('base_avg_formula','基期平均数公式',['技巧'],()=>R(
    '基期平均数的正确公式是？',
    '现期总量/现期份数 × (1+份数增长率)/(1+总量增长率)',
    ['现期总量/现期份数 × (1+份数增长率)/(1+总量增长率)','现期总量/现期份数 × (1+总量增长率)/(1+份数增长率)','现期份数/现期总量 × (1+份数增长率)','现期平均数 × 总量增长率'],
    '总量在分子，份数在分母；增长率交叉。',
    '基期总量=现期总量/(1+R总)，基期份数=现期份数/(1+R份)，相除后为现期平均数×(1+R份)/(1+R总)。'
  ));

  const d=Q('avg_vs_ratio','别和比重混淆',['基础'],()=>R(
    '“平均功率”应识别为哪类口径？',
    '总功率 ÷ 销量',
    ['总功率 ÷ 销量','某部分 ÷ 总功率','销量 ÷ 总功率','增长率相减'],
    '平均功率是每台/每件的功率。',
    '平均数与比重容易混，但必须不混淆。凡是“平均每个、每家、亩产量、平均功率”，先找总量和份数。'
  ));

  const e=Q('base_avg_variable','基期平均变量',['运用'],()=>{
    const total=n(2400,3600), copies=n(400,800), rt=n(6,12), rc=n(2,5);
    const ans='现期平均数 × (1+份数增速)/(1+总量增速)';
    return R(
      `现期总功率${total}，销量${copies}；总功率增速${pct(rt)}，销量增速${pct(rc)}。求基期平均功率，修正项应怎么放？`,
      ans,
      op(ans,['现期平均数 × (1+总量增速)/(1+份数增速)','现期平均数 × 总量增速/份数增速','只用现期总功率/销量']),
      '基期平均数公式要交叉。',
      `平均功率=总功率/销量，所以销量是份数，修正项为(1+${pct(rc)})/(1+${pct(rt)})。`
    );
  });

  const e2=Q('base_avg_direction','基期平均修正方向',['运用'],()=>{
    const rt=n(8,16), rs=n(1,7);
    return R(
      `某指标现期平均数为A，总量增长率${pct(rt)}，份数增长率${pct(rs)}。基期平均数相对现期平均数怎样？`,
      '更小',
      ['更小','更大','相等','无法判断'],
      '看修正项(1+R份)/(1+R总)。',
      `R份=${pct(rs)}小于R总=${pct(rt)}，所以修正项小于1，基期平均数小于现期平均数。`
    );
  });

  const f=Q('avg_denominator_pick','分母选择变量',['运用'],()=>{
    const modes=[['亩产量','播种面积或种植面积','总产量÷面积'],['平均每家市值','上市公司家数','总市值÷公司家数'],['平均功率','销量','总功率÷销量']];
    const m=modes[n(0,modes.length-1)];
    return R(
      `求“${m[0]}”时，分母应该找什么？`,
      m[1],
      op(m[1],['总人口','增长率','利润额']),
      '平均数的分母是份数。',
      `${m[0]}对应的列式是${m[2]}。`
    );
  });

  const g=Q('avg_not_ab_confusion','不混记AB',['驿站'],()=>R(
    '为什么本课把基期比重和基期平均数分开背，而不只用A/B统一记？',
    '避免实战时把部分整体、总量份数代乱',
    ['避免实战时把部分整体、总量份数代乱','因为两个公式完全无关','因为平均数没有增长率','因为比重不能计算'],
    '形式相似不等于口径相同。',
    '比重是部分/整体；平均数是总量/份数。统一字母看似省事，但容易把R部、R整、R总、R份代错。'
  ));

  const route=C('avg_variable_route','变量训练｜单位口径基期与分母',['运用'],[a2,a3,b,b2,b3,b4,c,d,e,e2,f,g],'all');
  add(C('data_average_card','平均数系统｜口径与基期',['技巧'],[a,route],'all'));
})();
