  const g=Q('ratio_change_bound','变化幅度边界',['技巧'],()=>R(
    '比重变化公式中，若R部为正且现期比重小于1，变化幅度通常与 |R部-R整| 有什么关系？',
    '小于 |R部-R整|',
    ['小于 |R部-R整|','一定等于 |R部-R整|','一定大于 |R部-R整|','与增长率无关'],
    '现期比重<1，且1+R部>1。',
    '比重变化=现期比重×(R部-R整)/(1+R部)。当R部为正时，前面乘的比例会让幅度小于增长率差本身；若R部为负，不能机械套这个方向。'
  ));



  const g2=Q('ratio_bound_negative_rpart','R部为负的边界',['驿站'],()=>R(
    '使用“比重变化幅度小于|R部-R整|”这个结论时，为什么要注意R部是否为正？',
    '因为R部为负时，1+R部可能小于1，分母修正方向会变',
    ['因为R部为负时，1+R部可能小于1，分母修正方向会变','因为负增长率不能计算','因为比重一定为负','因为百分点不能相减'],
    '这个边界结论依赖1+R部>1。',
    '原课提醒：R部为正时分母大于1，幅度会被压小；若R部为负，分母小于1，不能机械套同一个小于号。'
  ));

  const g3=Q('ratio_bound_positive_explicit','正增长小于差值',['技巧'],()=>{
    const rp=n(8,16), rw=rp+n(1,5);
    const ans=`下降幅度小于${(rw-rp).toFixed(1)}个百分点`;
    return R(
      `某比重变化中，R部=${pct(rp)}，R整=${pct(rw)}，且现期比重<1。变化幅度与${pct(rw-rp)}相比怎样？`,
      ans,
      op(ans,[`下降幅度大于${(rw-rp).toFixed(1)}个百分点`,`一定等于${(rw-rp).toFixed(1)}个百分点`,'无法判断升降']),
      'R部为正时，1+R部>1，即分母比一大；现期比重又小于1。',
      `幅度=现期比重×|R部-R整|/(1+R部)。前面小于1，分母也会压小结果，所以幅度小于增长率差${(rw-rp).toFixed(1)}个百分点。`
    );
  });

  const g4=Q('ratio_guess_small_value','应急猜小值',['驿站'],()=>R(
    '比重变化题已经判定“下降”，剩余两个下降选项为0.7和1.5个百分点，实在没时间时原课给的应急猜法是什么？',
    '猜数值小的0.7个百分点',
    ['猜数值小的0.7个百分点','猜数值大的1.5个百分点','改猜上升选项','直接放弃'],
    '这是应急，不替代正常估算。',
    '原课说先判断上升下降；有时间继续估算，没时间时比重变化题倾向猜数字小的，因为比重变化常比增长率差更小。'
  ));

  const h=Q('ratio_vs_multiple_boundary','比重与倍数边界',['驿站'],()=>R(
    '比重变化“小于|R部-R整|”这个经验为什么不能无脑推广到所有A/B比值题？',
    '因为倍数题的A/B可能大于1，不再是比重',
    ['因为倍数题的A/B可能大于1，不再是比重','因为增长率不能相减','因为所有比值都无公式','因为百分点不能比较'],
    '比重的现期比重小于1，倍数不一定。',
    '原课提醒：A是B的倍数或A/B比值变化时，A/B可能大于1，特殊情况下甚至会抵消成R部-R整，不能把比重边界机械套过去。'
  ));

  const route=C('ratio_variable_route','变量训练｜公式升降混合与边界',['运用'],[b,b2,b3,d,d2,d3,e,e2,e3,f,f2,f3,g,g2,g3,g4,h],'all');
  add(C('data_ratio_card','比重系统｜基期变化与陷阱',['技巧'],[a,route,c,c2,c3],'all'));
