(function(){
  const {R,Q,C,add,op,n,pk,r1}=EX;

  const q1=Q('line_good_base','改好数',['技巧'],()=>{
    const raw=pk([2733,3806,4503,1419]);
    const base=raw>3000?Math.round(raw/100)*100:Math.round(raw/10)*10;
    const dir=base>raw?'上升':'下降';
    const pct=r1(Math.abs(base-raw)/raw*100);
    const ans=`先把 ${raw} 改成好算的 ${base}，记录${dir}约 ${pct}%`;
    return R(
      `划线估算遇到分母 ${raw} 很别扭，第一步更节能的做法是什么？`,
      ans,
      op(ans,[`直接精算 ${raw} 的倒数`,`只删掉最后一位`,`把 ${raw} 当成 1 不记误差`]),
      '划线法不是乱改数，改成好数后要记住改动方向和大致误差。',
      `把不好算的 ${raw} 调成 ${base}，是为了让乘除变快；同时要记录它${dir}了约 ${pct}%，后面再判断答案该上调还是下调。`
    );
  });

  const q2=Q('line_error_direction','误差方向',['技巧'],()=>{
    const a=n(2,6), b=n(3,8);
    const ans='同向误差多抵消，反向误差多叠加';
    return R(
      `一个乘除式中，分子估大约 ${a}%，分母也估大约 ${b}%。判断最终误差时应记住什么？`,
      ans,
      op(ans,['同向误差多抵消，反向误差多叠加','分子分母误差永远相加','只看分子误差','误差方向不用管']),
      '分子和分母同向变化会互相抵消一部分；一个上一个下才会把误差放大。',
      '原文里“上升1%多、上升4%多，同号抵消后约上3%”就是这个思想：不必精确到每一位，但要知道方向。'
    );
  });

  const q3=Q('line_truncate_sync','截位同步',['基础'],()=>{
    const x=n(24000,98000), y=n(1200,9800);
    const ans='分子分母、前后式子要同步截位';
    return R(
      `把 ${x} 改写成 ${Math.round(x/100)}，同时另一个相关量 ${y} 应怎样处理才不乱量级？`,
      ans,
      op(ans,['分子分母、前后式子要同步截位','只改大的数，小的数不管','把所有数都看成百分数','只保留材料原文单位']),
      '省 0、移小数点、保留三位，都必须在同一表达式中保持口径一致。',
      '资料分析估算可以截位，但不能只改一边；否则式子量级会错。'
    );
  });

  const q4=Q('line_three_second','三秒误差感',['运用'],()=>{
    const d=n(300,900), addv=n(12,54);
    const pct=r1(addv/d*100);
    const ans=`约 ${pct}%`;
    return R(
      `${d} 增加 ${addv}，大约上升百分之几？`,
      ans,
      op(ans,[`约 ${r1(addv/(d+addv)*100)}%`,`约 ${r1((addv+d)/d*100)}%`,`约 ${r1(d/addv)}%`]),
      '这是划线法的基本功：快速看出“差量÷原数”。',
      `${addv}÷${d}≈${pct}%，考试中要训练到很快能判断误差量级。`
    );
  });

  add(C('line_estimation_card','划线法｜误差方向与截位',['技巧'],[q1,q2,q3,q4],'all'));
})();
