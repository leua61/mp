(function(){
  const {R,Q,C,add,op,pk,n}=EX;

  const q1=Q('core_time','时间先行',['基础'],()=>{
    const mat=n(2008,2020);
    const ask=mat-1;
    const ans='基期';
    return R(
      `材料给的是 ${mat} 年，题目问 ${ask} 年的指标。第一判断应是？`,
      ans,
      op(ans,['现期','基期','隔年','年均']),
      '先读问法里的时间点，再与材料时间比较。',
      `${ask} 年早于材料的 ${mat} 年，所以问的是过去值，即基期。`
    );
  });

  const q2=Q('core_object','对象后置',['基础'],()=>{
    const ans='先问求谁，再列式';
    return R(
      '资料分析读题时，时间判断之后，最节能的下一步是什么？',
      ans,
      op(ans,['直接计算所有数据','先问求谁，再列式','只看最大数字','跳过材料']),
      '时间解决“现期/基期”，对象解决“分子分母是谁”。',
      '先明确求谁的值、谁的比重、谁的平均数，再去材料中找对应量。'
    );
  });

  const q3=Q('core_structure','结构识别',['基础'],()=>{
    const items=[
      ['平均每家接待人数','总量÷份数'],
      ['某项占总额比例','部分÷整体'],
      ['贸易顺差','出口-进口'],
      ['同比增长率','增长量÷基期量'],
      ['增长量','现期量-基期量']
    ];
    const [stem,ans]=pk(items);
    return R(
      `题干出现“${stem}”，优先识别为哪种结构？`,
      ans,
      op(ans,items.map(x=>x[1])),
      '不要被材料数字牵着走，先把问法翻译成数学结构。',
      `“${stem}”对应结构是 ${ans}。`
    );
  });

  const q4=Q('core_options','选项定精度',['运用'],()=>{
    const ans='先看选项差距，再决定估算或精算';
    return R(
      '同样一道资料分析题，为什么有人能不完整计算也能选出答案？',
      ans,
      op(ans,['永远只直除','先看选项差距，再决定估算或精算','所有题都用拆分法','只背固定答案']),
      '选项决定你需要精确到首位、百分位，还是千分位。',
      '选项差距大时估到首位即可；选项极近时才需要拆分、直除或精算。'
    );
  });

  add(C('core_card','做题总纲｜时间对象题型',['基础'],[q1,q2,q3,q4],'sequence'));
})();
