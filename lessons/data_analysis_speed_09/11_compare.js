(function(){
  const {R,Q,C,add,op,n}=EX;

  const q1=Q('cmp_reference','参照系法',['技巧'],()=>{
    const den=n(220,360), num=den-n(10,40);
    const ans='看谁到 1 还差得少';
    return R(
      `多个分数都接近 1，例如 ${num}/${den} 这类。比较大小最节能的参照是什么？`,
      ans,
      op(ans,['看谁到 1 还差得少','把所有分母相乘','只看分子末位','直接平均']),
      '都小于1时，越接近1越大。',
      '接近同一个数时，用参照系法；都接近1，就比较到1还需上升多少。'
    );
  });

  const q2=Q('cmp_big_small','分子大分母小',['基础'],()=>{
    const ans='分子更大且分母更小者更大';
    return R(
      '比较两个正分数时，如果一个分数分子更大、分母更小，可以直接判断什么？',
      ans,
      op(ans,['分子更大且分母更小者更大','分子更大且分母更小者更小','无法比较','必须直除四位']),
      '同向对比：分子越大值越大，分母越大值越小。',
      '若同时满足分子大、分母小，就无需计算，直接更大。'
    );
  });

  const q3=Q('cmp_scale','倍数放缩',['技巧'],()=>{
    const ans='把接近倍数的一方放大或缩小后再比';
    return R(
      '两个分数分母约为 4 倍关系，课程里推荐的“小学生方法”是什么？',
      ans,
      op(ans,['把接近倍数的一方放大或缩小后再比','直接把两个分数相加','只看小数点后两位','把分子分母都去掉']),
      '让两个分数进入可直接比较的量级。',
      '先按明显倍数放缩，再观察是否出现分子大分母小、或首位明显不同。'
    );
  });

  const q4=Q('cmp_diff_frac','差分法',['技巧'],()=>{
    const ans='用分子差/分母差辅助比较';
    return R(
      '差分法比较两个分数时，差分数的基本形态是什么？',
      ans,
      op(ans,['用分子差/分母差辅助比较','用分子和/分母和代替答案','只比较分母差','把两个分数相乘']),
      '差分法是辅助判断大小，不是求原题数值。',
      '构造差分数后，与原来的大分数或小分数比较，可辅助判断两个分数大小。'
    );
  });

  const q5=Q('cmp_first_digit','首位边界',['运用'],()=>{
    const ans='一位数选项可能是四舍五入值';
    return R(
      '为什么选项只有 2、3、4、5 时，不能简单按“结果2开头就选2”？',
      ans,
      op(ans,['一位数选项可能是四舍五入值','首位法永远无效','两位数选项不能用首位','分母越大选项越小']),
      '2.8 四舍五入会写成 3。',
      '一位数选项常代表四舍五入后的整数；两位及以上首位不同，首位法才更稳。'
    );
  });


  const q6=Q('cmp_good_factor','好数因子',['技巧'],()=>{
    const ans='把因子转移成更好算的骨架';
    return R(
      '估算中遇到 45、125、11 这类好数时，所谓“三不看/因子意识”的核心是什么？',
      ans,
      op(ans,['把因子转移成更好算的骨架','所有数字都精确直除','只保留整数位','看到5就直接删掉']),
      '目的不是套名词，而是把分母或分子改成心算友好的等价结构。',
      '例如 45=5×9，可以把 5 的影响转到分子上，再按 9 来算；125 可联想到 1/8。核心是节能且控制误差。'
    );
  });


  add(C('compare_card','比大小｜参照首位差分',['技巧'],[q1,q2,q3,q4,q5,q6],'all'));
})();
