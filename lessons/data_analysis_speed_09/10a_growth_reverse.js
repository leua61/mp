(function(){
  const {R,Q,C,add,op,n,pk,r1}=EX;

  const q1=Q('growth_reverse_check_core','反代法骨架',['技巧'],()=>{
    const now=n(30000,38000), rate=n(6,12), g=n(2200,3400);
    const ans=`先算 ${now}-${g}，再看结果×${rate}% 是否约等于 ${g}`;
    return R(
      `现期量 ${now}，增长率 ${rate}%。若选项候选增长量为 ${g}，反代法第一步是什么？`,
      ans,
      op(ans,[`直接用 ${now}×${rate}% 作为答案`,`先算 ${now}+${g}，再除以 ${rate}%`,`只比较 ${g} 的首位`]),
      '候选增长量若正确，则基期量≈现期量-增长量，且基期量×增长率≈增长量。',
      `反代法是拿选项倒回去验证：基期≈${now}-${g}，再用基期×${rate}% 检查是否回到候选增长量。`
    );
  });

  const q2=Q('growth_reverse_when','何时用反代',['运用'],()=>{
    const ans='求增长量且选项接近、常规计算反复时';
    return R(
      '增长量题里，什么时候反代法/代入法比公式硬算更节能？',
      ans,
      op(ans,['求增长量且选项接近、常规计算反复时','所有平均数题','所有比重题','选项完全无关时']),
      '原文强调这种复杂增长量题，用反代比拆分、假设分配少走步骤。',
      '候选增长量给得很像时，直接代入候选值验证，比从现期量×r/(1+r)精算更快。'
    );
  });

  const q3=Q('growth_reverse_multiply_split','乘法拆开',['技巧'],()=>{
    const base=n(2500,3900), rate=pk([67,78,87,96]);
    const tens=Math.floor(rate/10)*10, ones=rate-tens;
    const ans=`拆成 ×${tens}+×${ones}`;
    return R(
      `反代检查中需要快速算 ${base}×${rate}。原文式的节能乘法是？`,
      ans,
      op(ans,[`拆成 ×${tens}+×${ones}`,'必须列竖式算到底','只乘个位不乘十位','把乘法改成除法']),
      '把两位数乘法拆成整十部分和个位部分。',
      `${rate}=${tens}+${ones}，所以 ${base}×${rate}=${base}×${tens}+${base}×${ones}，口算压力更小。`
    );
  });

  const q4=Q('growth_chase_guess_two','超过类没时间猜',['运用'],()=>{
    const ans='先估初始差距和每年追赶量，没时间时才用近似猜年';
    return R(
      '“保持增长率不变，哪年超过/达到”类题，原文里的态度是什么？',
      ans,
      op(ans,['先估初始差距和每年追赶量，没时间时才用近似猜年','永远固定猜两年','只看年份尾数','只看增长率大的那项']),
      '原文提到“没时间才猜”，有时间要用追及思路估。',
      '这类题本质是追及：先看差距，再看快者每年比慢者多追多少。若时间极紧，才用经验猜法。'
    );
  });

  add(C('growth_reverse_card','增长量｜反代与超过',['技巧'],[q1,q2,q3,q4],'all'));
})();
