(function(){
  const {R,Q,C,add,op,n,pk}=EX;

  const q1=Q('mix_identity_cumulative','累计就是混合',['基础'],()=>{
    const end=n(3,12);
    const month=n(2,end);
    const ans=`1-${end}月累计是混合，${month}月是部分`;
    return R(
      `材料给“1-${end}月累计增长率”和“${month}月当月增长率”。谁是混合值，谁是部分值？`,
      ans,
      op(ans,[`${month}月是混合，1-${end}月累计是部分`,'二者都是混合值','二者都是部分值']),
      '累计包含多个期间，当月只是其中一个部分。',
      `1-${end}月累计由若干月份合成，所以累计增长率是混合值；${month}月当月是其中一个部分。`
    );
  });

  const q2=Q('mix_unknown_other_side','求另一部分位置',['基础'],()=>{
    const rMix=n(12,19), rKnown=rMix+n(2,7);
    const ans=`未知部分增长率应小于 ${rMix}%`;
    return R(
      `整体混合增长率为 ${rMix}%，已知一个部分为 ${rKnown}%。求另一个部分时，未知值大致在哪一侧？`,
      ans,
      op(ans,[`未知部分增长率应大于 ${rKnown}%`,`未知部分增长率应小于 ${rMix}%`,`未知部分一定等于 ${rMix}%`]),
      '混合值夹在两个部分之间。已知部分在混合值上方，另一部分就应在混合值下方。',
      `${rMix}% 是混合值，${rKnown}% 是一个部分且高于混合值，所以另一个部分必须低于 ${rMix}%。`
    );
  });

  const q3=Q('mix_distance_lean','距离偏向',['技巧'],()=>{
    const ans='混合值到量大部分的距离更小';
    return R(
      '混合值“偏向量大的部分”，落实到选项距离上是什么意思？',
      ans,
      op(ans,['混合值到量大部分的距离更小','混合值一定等于量大部分','量大部分一定增长率更高','距离与量无关']),
      '偏向不是直接选量大的增长率，而是看混合值离哪边更近。',
      '量大的部分权重更大，整体混合值被拉向它，所以混合值到量大部分的距离更小。'
    );
  });

  const q4=Q('mix_need_estimate_ratio','两原理不够再估量比',['运用'],()=>{
    const ans='再估两部分量之比';
    return R(
      '如果混合第一原理“夹逼”和第二原理“偏向量大者”仍不能唯一排除选项，下一步应做什么？',
      ans,
      op(ans,['再估两部分量之比','直接平均两个增长率','直接选最大项','放弃混合原理']),
      '原文里先用夹逼和偏向；还不够，再看量比。',
      '量比决定混合值离两端的距离比例。先定范围和方向，仍不够时再估量比，不必一上来精算。'
    );
  });

  add(C('mix_identity_card','混合识别｜整体与部分',['基础'],[q1,q2,q3,q4],'all'));
})();
