(function(){
  const {R,Q,C,add,op,n,r1}=EX;

  const q1=Q('frac_common_den','普通分母通分',['技巧'],()=>{
    const den1=n(720,860), up=n(20,45), den2=den1-up;
    const num2=n(220,330);
    const pct=r1(up/den2*100);
    const ans=`把 ${den2} 调到 ${den1}，分子同步上升约 ${pct}%`;
    return R(
      `遇到 a/${den1} - ${num2}/${den2}，两个分母接近。优先怎么处理第二个分数？`,
      ans,
      op(ans,[`直接把两个分母都看成 1000`,`把 ${den2} 调到 ${den1}，分子不变`,`把 ${den1} 调到 ${den2}，只改分母`]),
      '通分时分母怎么变，分子也要同步按比例变。',
      `${den2} 到 ${den1} 上升约 ${pct}%，所以第二个分子的等价值也要同步上升约 ${pct}%。`
    );
  });

  const q2=Q('frac_growth_den','1+r分母',['技巧'],()=>{
    const ans='优先转混合原理';
    return R(
      '两个分数做和或做差，分母都是 1+r 形式，且增长率差距不大。优先用什么思路？',
      ans,
      op(ans,['优先转混合原理','一律拆成二分之一','只看分母大小','直接放弃']),
      '1+r 分母对应“由现期回基期”，可看成整体和部分的混合增长。',
      '当增长率同号且差距不大时，混合原理能快速估出等价增长率，再排选项。'
    );
  });

  const q3=Q('frac_error_amplify','误差放大',['基础'],()=>{
    const ans='加减法会放大误差';
    return R(
      '为什么两个分数做差时，单个分数估得很准，最终答案仍可能偏很多？',
      ans,
      op(ans,['加减法会放大误差','乘法一定无误差','分母越大越不准','百分号不能估算']),
      '差值可能很小，局部误差相减后占整体比例变大。',
      '分数加减尤其是做差，会把原来千分级的局部误差放大成几个百分点的整体误差。'
    );
  });

  const q4=Q('frac_split_when','拆分法场景',['技巧'],()=>{
    const ans='分数加减且选项很近';
    return R(
      '拆分法最应该留给哪类资料分析计算？',
      ans,
      op(ans,['单个 A/B 且选项很远','分数加减且选项很近','所有比重题','所有混合题']),
      '单个 A/B 往往划线更快；加减法才更容易放大误差。',
      '当 A/B±C/D 且选项差距在 1%—5%以内时，拆分或直除更稳。'
    );
  });

  const q5=Q('frac_single_ab','单个分数',['运用'],()=>{
    const ans='划线或首位估算更节能';
    return R(
      '单独一个 A/B，且选项差距明显时，课程更推荐什么？',
      ans,
      op(ans,['划线或首位估算更节能','必须拆分到四位','必须通分','必须用混合原理']),
      '拆分法不是为了炫技，只有在必要时才用。',
      '单个分数没有加减放大误差，通常用划线、首位或直除即可。'
    );
  });

  add(C('fraction_card','分数加减｜通分拆分边界',['技巧'],[q1,q2,q3,q4,q5],'all'));
})();
