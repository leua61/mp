(function(){
  const {R,Q,C,add,op,n,r1}=EX;

  const q1=Q('ratio_current','现期比重',['基础'],()=>{
    const part=n(1200,4800), whole=part+n(800,5200);
    const ans='部分÷整体';
    return R(
      `现期部分量为 ${part}，整体量为 ${whole}。现期比重的基本结构是？`,
      ans,
      op(ans,['整体÷部分','部分÷整体','增长量÷现期','现期÷基期']),
      '“占”字前面通常是部分，后面是整体。',
      `现期比重=${part}÷${whole}，即部分÷整体。`
    );
  });

  const q2=Q('ratio_base_formula','基期比重公式',['技巧'],()=>{
    const rp=n(10,25), rw=n(4,15);
    const ans=`现期比重×(1+${rw}%)/(1+${rp}%)`;
    return R(
      `部分增长率 ${rp}%，整体增长率 ${rw}%。基期比重应如何由现期比重修正？`,
      ans,
      op(ans,[`现期比重×(1+${rp}%)/(1+${rw}%)`,`现期比重×(${rp}%-${rw}%)`,`现期比重÷(1+${rw}%)`]),
      '基期比重=现期比重×整体增长因子÷部分增长因子。记作交叉对应。',
      `基期比重=现期比重×(1+r整体)/(1+r部分)，所以为 ${ans}。`
    );
  });

  const q3=Q('ratio_size','基期现期大小',['运用'],()=>{
    const rw=n(6,12), rp=rw+n(2,10);
    const ans='现期比重大于基期比重';
    return R(
      `部分增长率 ${rp}%，整体增长率 ${rw}%。现期比重与基期比重谁大？`,
      ans,
      op(ans,['现期比重大于基期比重','现期比重小于基期比重','二者一定相等','无法判断']),
      '部分涨得比整体快，份额会提高。',
      `${rp}%>${rw}%，部分增长更快，所以现期比重更大，基期比重更小。`
    );
  });

  const q4=Q('ratio_pp','百分点',['基础'],()=>{
    const a=n(40,60), b=a+n(2,8);
    const ans=`增加 ${b-a} 个百分点`;
    return R(
      `某比重由 ${a}% 变为 ${b}%。应表述为？`,
      ans,
      op(ans,[`增加 ${b-a}%`,`增加 ${r1((b-a)/a*100)}%`,`增加 ${b} 个百分点`]),
      '两个百分数直接相减，单位是百分点。',
      `${b}%-${a}%=${b-a} 个百分点，不是“增长 ${b-a}%”。`
    );
  });

  const q5=Q('ratio_trap','现期陷阱',['运用'],()=>{
    const ans='现期比重陷阱';
    return R(
      '题目问基期比重，选项里常出现“直接用现期部分÷现期整体”的结果。这个选项通常是什么？',
      ans,
      op(ans,['现期比重陷阱','平均数陷阱','隔年增长陷阱','顺差陷阱']),
      '求过去，却直接用现在的数据，通常就是干扰项。',
      '基期比重必须乘增长率修正项；只算现期比重常是出题人设置的陷阱。'
    );
  });


  const q6=Q('ratio_correction_gap','修正项定位',['技巧'],()=>{
    const rw=n(8,14), rp=rw+n(1,4);
    const gap=rp-rw;
    const ans='看增长因子修正项与现期陷阱的距离';
    return R(
      `求基期比重时，部分增长 ${rp}%，整体增长 ${rw}%，选项里有一个现期比重陷阱。近选项判断应重点看什么？`,
      ans,
      op(ans,['看增长因子修正项与现期陷阱的距离','只看材料第一句','只看部分量大小','直接选最大的比重']),
      '基期比重=现期比重×(1+r整体)/(1+r部分)，答案与现期陷阱的差距来自修正项。',
      `这里部分增速比整体高约 ${gap} 个百分点，现期比重通常比基期比重大一点；近选项可用这个修正差距定位答案。`
    );
  });


  add(C('ratio_card','比重｜现期基期变化',['基础'],[q1,q2,q3,q4,q5,q6],'all'));
})();
