(function(){
  const {R,Q,C,add,op,n,pk,r1}=EX;

  const q1=Q('den_to_one_growth','普通分母转1开头',['技巧'],()=>{
    const d=n(720,890), k=n(6,8);
    const scaled=r1(d/(k*100));
    const ans='可以转，但通常不是首选';
    return R(
      `普通分母 ${d} 若分子分母同除以 ${k*100}，分母可变成约 ${scaled}，看成 1 附近的增长因子。这个思路应如何定位？`,
      ans,
      op(ans,['可以转，但通常不是首选','必须所有题都这样转','这种转化一定错误','只改分母不用改分子']),
      '原文说普通分母也能转成混合原理，但步骤更绕。',
      `把普通分母变成 1 开头，可以借混合原理理解；但要同步处理分子分母，且操作偏麻烦，所以实战常优先通分或拆分。`
    );
  });

  const q2=Q('den_scale_close','先放缩接近',['技巧'],()=>{
    const small=n(210,260), big=small*2+n(8,24);
    const ans=`先把 ${small} 放大 2 倍接近 ${big}`;
    return R(
      `两个分数分母约为 ${small} 和 ${big}，直接通分不顺。第一步更节能的是？`,
      ans,
      op(ans,[`直接把两者都看成 400`,`只把 ${big} 删掉一位`,`分母不变直接分子相减`]),
      '分母差距较大但有倍数关系时，先放缩到接近，再通分。',
      `${small}×2=${small*2}，已经接近 ${big}。同步把对应分子也乘 2 后，两个分母就适合再做通分比较。`
    );
  });

  const q3=Q('den_no_double_rough','不要双粗改',['运用'],()=>{
    const ans='不要把两个分母都粗暴改成同一个整百数';
    return R(
      '两个分数做差时，为什么不建议直接把 421 和 452 都粗暴看成 400？',
      ans,
      op(ans,['不要把两个分母都粗暴改成同一个整百数','这样一定会完全精确','因为400不能做分母','因为分数做差不能估算']),
      '两个分数做差会放大误差，粗暴双改更危险。',
      '更稳的是先让一个分母变成另一个分母，或先倍数放缩接近；同时分子也按同一比例变化。'
    );
  });

  const q4=Q('den_direction_good_factor','通分方向看好算',['技巧'],()=>{
    const ans='优先改向更好算、有好因子的分母';
    return R(
      '两个分母都可以互相调整时，原文为什么倾向把 421 调到 452，而不是反过来？',
      ans,
      op(ans,['优先改向更好算、有好因子的分母','只能把小数改成大数','只能把大数改成小数','方向完全随机不用考虑']),
      '452 可进一步看作 45 附近，45 有 5×9 的好算骨架。',
      '通分方向不只是升或降的个人习惯，还要看目标数是否方便后续心算，例如是否接近 45、125、11 等好数骨架。'
    );
  });

  const q5=Q('den_sync_num','放缩必须同步分子',['基础'],()=>{
    const m=n(2,3), num=n(260,360), den=n(180,260);
    const ans=`分子也乘 ${m}`;
    return R(
      `把分数 ${num}/${den} 的分母乘 ${m}，准备与另一个分母接近。为了等价，分子应怎样？`,
      ans,
      op(ans,[`分子也乘 ${m}`,'分子保持不变',`分子除以 ${m}`,'分子加上分母']),
      '分数等价变形，分子分母同乘或同除。',
      `分母乘 ${m} 后，分子也必须乘 ${m}，否则数值被改坏。`
    );
  });

  add(C('denominator_strategy_card','分母处理｜转化放缩通分',['技巧'],[q1,q2,q3,q4,q5],'all'));
})();
