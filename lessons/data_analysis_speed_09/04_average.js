(function(){
  const {R,Q,C,add,op,n,r1}=EX;

  const q1=Q('avg_basic','普通平均数',['基础'],()=>{
    const total=n(3200,9600), cnt=n(20,80);
    const ans='总量÷份数';
    return R(
      `某类机构接待 ${total} 万人次，共 ${cnt} 家。平均每家接待人数的基本列式是什么？`,
      ans,
      op(ans,['份数÷总量','总量÷份数','部分÷整体','增长量÷基期']),
      '“平均每家”就是把总量分摊到家数上。',
      `平均数本质是总量除以份数，即 ${total}÷${cnt}。`
    );
  });

  const q2=Q('avg_share_cancel','占比约分',['技巧'],()=>{
    const a=n(55,88), b=100-a, c=n(30,55), d=100-c;
    const ans=`${a}/${b} × ${d}/${c}`;
    return R(
      `甲接待量占 ${a}%，机构数占 ${c}%；乙为剩余部分。甲平均数是乙平均数的多少倍，应先化成哪种骨架？`,
      ans,
      op(ans,[`${a}/${c} × ${b}/${d}`,`${c}/${d} × ${b}/${a}`,`${a}/${b} × ${c}/${d}`]),
      '平均数倍数 = 甲总量/甲份数 ÷ 乙总量/乙份数。公共总量和公共机构数可约掉。',
      `乙接待量占 ${b}%，乙机构数占 ${d}%。所以是 (${a}/${c})÷(${b}/${d})=${a}/${b}×${d}/${c}。`
    );
  });

  const q3=Q('avg_growth_formula','增长率公式',['基础'],()=>{
    const rt=n(12,28), rc=n(2,11);
    const ans=`(${rt}%-${rc}%)/(1+${rc}%)`;
    return R(
      `总成交额增长 ${rt}%，合同项数增长 ${rc}%。平均每项成交额增长率应列为？`,
      ans,
      op(ans,[`(${rt}%-${rc}%)/(1+${rt}%)`,`(${rc}%-${rt}%)/(1+${rc}%)`,`${rt}%-${rc}%`]),
      '平均数=总量÷份数，增长率用“总减份，除以1+份”。',
      `平均数增长率 = (r总-r份)/(1+r份)，所以列式为 ${ans}。`
    );
  });

  const q4=Q('avg_growth_trend','趋势判断',['运用'],()=>{
    const rt=n(5,18), rc=rt+n(1,8);
    const ans='下降';
    return R(
      `总量增长 ${rt}%，份数增长 ${rc}%。平均数变化方向是？`,
      ans,
      op(ans,['上升','下降','不变','无法判断']),
      '平均数增长率看 r总-r份 的符号。',
      `${rt}%<${rc}%，总量追不上份数，平均数下降。`
    );
  });

  add(C('average_card','平均数｜公式与倍数',['基础'],[q1,q2,q3,q4],'all'));
})();
