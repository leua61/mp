(function(){
  const {R,Q,C,add,op,n,r1}=EX;

  const q1=Q('err_precision','精度选择',['运用'],()=>{
    const ans='按选项差距决定';
    return R(
      '一道题到底估算到首位、百分位还是千分位，最主要由什么决定？',
      ans,
      op(ans,['按选项差距决定','按材料篇幅决定','按数字是否好看决定','永远精算']),
      '考试不是为了算得最细，而是为了区分选项。',
      '选项差距大，估算即可；选项极近，才提高精度。'
    );
  });

  const q2=Q('err_option_expand','放大选项误差',['技巧'],()=>{
    const ans='先整体减 1，再比较尾部差异';
    return R(
      '选项为 1.236、1.243、1.253 这类千分级比值时，课程给出的处理思路是什么？',
      ans,
      op(ans,['先整体减 1，再比较尾部差异','直接看个位','全部看成1','只选中间项']),
      '把共同的 1 去掉，差异会被放大。',
      '原式和选项同步减 1，可把 0.236、0.243、0.253 放大成 236、243、253 来比较。'
    );
  });

  const q3=Q('err_relative_absolute','相对差不是绝对差',['基础'],()=>{
    const ans='把相对差当成绝对差';
    return R(
      '把 21.4% 近似为 20% 后，认为“最后只要加回 1.4%”错在哪里？',
      ans,
      op(ans,['把相对差当成绝对差','没有四舍五入','百分号不能近似','必须除以1000']),
      '21.4 到 20 的变化不是结果直接少 1.4 个点。',
      '近似后的误差要看相对变化比例，不能把数字差直接加到最终答案上。'
    );
  });

  const q4=Q('err_close_options','近选项纸老虎',['运用'],()=>{
    const ans='先动笔估范围，不要被近选项吓住';
    return R(
      '两个选项看起来非常接近时，正确的实战态度是什么？',
      ans,
      op(ans,['先动笔估范围，不要被近选项吓住','直接放弃','必选近选项之一','一定全程精算']),
      '近选项可能只是干扰，答案未必在它们之间。',
      '先估出答案大概范围，可能发现看似接近的两个选项都不是答案。'
    );
  });

  const q5=Q('err_unit','单位判断',['基础'],()=>{
    const ans='先看题干问绝对量还是相对量';
    return R(
      '选项同时出现“140”和“89.6%”，题干问“多了多少用水量”。应先做什么？',
      ans,
      op(ans,['先看题干问绝对量还是相对量','直接选百分号','直接选数字最大项','只看材料第一句']),
      '有单位的“量”通常是绝对差；“多百分之几/是几倍”才是相对量。',
      '题干问“用水量多多少”，先按绝对量理解；若选项混乱，再警惕非标准题需要额外核算。'
    );
  });

  add(C('error_options_card','误差选项｜估算精算切换',['运用'],[q1,q2,q3,q4,q5],'all'));
})();
