(function(){
  const {R,Q,C,add,op,n,r1}=EX;

  const q1=Q('base_diff_expression','基期差列式',['技巧'],()=>{
    const a=n(2600,4200), b=a-n(400,1200), ra=n(18,36), rb=ra-n(1,8);
    const ans=`${a}/(1+${ra}%)-${b}/(1+${rb}%)`;
    return R(
      `现期甲为 ${a}、增长 ${ra}%；现期乙为 ${b}、增长 ${rb}%。求基期甲比乙多多少，列式是？`,
      ans,
      op(ans,[`${a}-${b}`,`${a}/(1+${rb}%)-${b}/(1+${ra}%)`,`${b}/(1+${rb}%)-${a}/(1+${ra}%)`]),
      '求基期差，必须先分别回基期，再相减。',
      `基期甲=${a}/(1+${ra}%)，基期乙=${b}/(1+${rb}%)，所以基期差为 ${ans}。`
    );
  });

  const q2=Q('base_diff_current_trap','现期差陷阱',['运用'],()=>{
    const a=n(2600,3900), b=a-n(500,1100);
    const ans='现期差陷阱';
    return R(
      `题目问基期差，但选项中出现 ${a}-${b}=${a-b} 附近的数。这个选项通常是什么？`,
      ans,
      op(ans,['现期差陷阱','平均数增长率','年均增长量','顺差公式']),
      '题目问过去，却直接拿现期两个量相减。',
      '基期差要分别除以增长因子；现期差只是在当前时点相减，常被设置为干扰项。'
    );
  });

  const q3=Q('base_diff_mix_positive','等价增长率方向',['技巧'],()=>{
    const ans='看现期差对应的等价增长率是正还是负，再决定能否用混合';
    return R(
      '两个 1+r 分母做差时，课程为什么会先想“现期差÷(1+等价增长率)”？',
      ans,
      op(ans,['看现期差对应的等价增长率是正还是负，再决定能否用混合','因为现期差永远就是答案','因为增长率不用看','因为分母可以直接删掉']),
      'A/(1+r1)-B/(1+r2) 可尝试整理成现期差对应的基期差，但等价增长率要可控。',
      '如果等价增长率温和，混合思路很快；如果被两个大数相减放大到极端，就不能硬用。'
    );
  });

  const q4=Q('base_diff_small_negative_ok','小负数仍可勉强用',['运用'],()=>{
    const ans='小负数勉强可用，极端负数不要用';
    return R(
      '两个分数做差转混合时，若等价增长率是 -4% 左右；另一次却可能是 -120%。两者处理有什么区别？',
      ans,
      op(ans,['小负数勉强可用，极端负数不要用','只要负数都不能用','负数越大越精确','二者完全一样']),
      '原文最后总结了这个边界。',
      '增长率一正一负不一定立刻废掉；小负数还能近似，但极端负值说明混合已经失真，应回到划线、直除或拆分。'
    );
  });

  add(C('base_difference_card','基期差｜现期差与边界',['技巧'],[q1,q2,q3,q4],'all'));
})();
