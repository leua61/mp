(function(){
  const {R,Q,C,add,op,n,r1}=EX;

  const q1=Q('trade_structure','顺差结构',['基础'],()=>{
    const ans='出口-进口';
    return R(
      '题干问“贸易顺差约为多少”，最先应翻译成什么结构？',
      ans,
      op(ans,['出口-进口','进口-出口','出口+进口','出口÷进口']),
      '顺差是出口大于进口的差额。',
      '贸易顺差=出口额-进口额；逆差则是进口大于出口。'
    );
  });

  const q2=Q('trade_base_expression','基期顺差列式',['技巧'],()=>{
    const ex=n(420,680), im=ex-n(20,90), re=n(8,18), ri=re+n(5,18);
    const ans=`${ex}/(1+${re}%)-${im}/(1+${ri}%)`;
    return R(
      `现期出口 ${ex}，增长 ${re}%；现期进口 ${im}，增长 ${ri}%。求基期顺差应列为？`,
      ans,
      op(ans,[`${ex}-${im}`,`${ex}/(1+${ri}%)-${im}/(1+${re}%)`,`${im}/(1+${ri}%)-${ex}/(1+${re}%)`]),
      '先分别回到基期，再做出口-进口。',
      `基期出口=${ex}/(1+${re}%)，基期进口=${im}/(1+${ri}%)，顺差为 ${ans}。`
    );
  });

  const q3=Q('trade_mix_boundary','顺差混合边界',['运用'],()=>{
    const ans='若等价增长率极端为负，就不要硬用混合近似';
    return R(
      '出口和进口两个基期量相减时，若把顺差转混合后算出类似 -100% 以上的等价增长率，应怎么处理？',
      ans,
      op(ans,['若等价增长率极端为负，就不要硬用混合近似','继续套十字交叉直到出数','直接选现期顺差','把负号去掉再算']),
      '顺差是两个大数相减，差额小，增长率可能被放大到很极端。',
      '原文顺差题强调：一正一负且负值极端时，混合原理不精确，应改用划线、直除或拆分。'
    );
  });

  const q4=Q('trade_first_digit_fallback','顺差估算回退',['技巧'],()=>{
    const ans='选项差距大时直接分别估两个基期量再相减';
    return R(
      '基期顺差题不适合混合近似，但选项首位差距较大。最节能的回退方法是什么？',
      ans,
      op(ans,['选项差距大时直接分别估两个基期量再相减','必须算到小数点后三位','只用现期顺差','只比较增长率大小']),
      '方法失真时要换方法，不是硬套。',
      '分别用 1/(1+r) 的好数近似算出口基期、进口基期，再做差；选项差距大时足够。'
    );
  });

  add(C('trade_surplus_card','顺差｜结构与混合边界',['技巧'],[q1,q2,q3,q4],'all'));
})();
