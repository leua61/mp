(function(){
  const {pk,n,op,R,Q,C,add}=EX;

  const q1=Q('da_time_range_not_year_only','时间不只看年份',['基础'],()=>{
    const now=pk(['上半年','1—6月']); const asked=pk(['1—4月','1—5月']);
    return R(
      `材料给的是2015年${now}，题目问2015年${asked}增速。判断时间口径时，只看年份够吗？`,
      '不够，还要看累计区间或月份范围',
      op('不够，还要看累计区间或月份范围',['够，只要年份相同就是现期','直接按全年处理','只看对象不看时间']),
      '资料分析里的时间点包含年份，也包含上半年、1—4月、1—5月等范围。',
      `2015年${now}和2015年${asked}不是同一个累计区间。原课中“上半年降幅比1—4月扩大”就要用百分点修正，而不是只说年份相同。`
    );
  });

  const q2=Q('da_average_unit_identify','平均数由单位识别',['基础'],()=>{
    const money=n(80,150), tons=n(20,60);
    return R(
      `题目问“平均每吨出口价格”。材料同时给出口额 ${money} 亿元、出口量 ${tons} 万吨。平均数分子应选什么？`,
      '出口额',
      op('出口额',['出口量','出口额和出口量相加','同比增长率']),
      '平均每吨价格的单位是“钱/吨”，分子必须是钱。',
      `平均每吨价格=出口额÷出口量。原课专门提醒出口价格看“额”的增长率，出口量看“量”的增长率。`
    );
  });

  const q3=Q('da_ratio_bias_after_first_digit','首位后看偏向',['技巧'],()=>{
    const denom=n(360,420); const numer=n(65,85);
    const val=numer/denom;
    const ans=val>0.18?'接近20%':'接近18%';
    return R(
      `估算 ${numer}÷${denom}，首位判断为一成多后，还应进一步看什么？`,
      '看它更靠近18%还是20%等选项档位',
      op('看它更靠近18%还是20%等选项档位',['首位一开头就停止','直接看成1%','只比较分子尾数']),
      '首位只能排除大范围；选项接近时要看偏向。',
      `${numer}/${denom}≈${(val*100).toFixed(1)}%，${ans}。原课 71÷388 就是先看一开头，再判断更接近二成。`
    );
  });

  const q4=Q('da_opposite_rate_not_same_sum','正负同幅不等于合计不变',['技巧'],()=>{
    const a=n(80,160), b=n(60,140), r=pk([6,8,9,12]);
    return R(
      `甲现期 ${a}、同比下降 ${r}%；乙现期 ${b}、同比增长 ${r}%。能否直接认为“甲乙现期和与上年同期和相同”？`,
      '不能，基期分母不同，不能直接抵消',
      op('不能，基期分母不同，不能直接抵消',['能，增长率一正一负同幅必抵消','只要两个现期相同才不能抵消','只看百分点即可']),
      '现期合计与基期合计比较，要分别倒推，不能只看增长率符号。',
      `甲基期=${a}÷(1-${r}%)，乙基期=${b}÷(1+${r}%)；两个分母不同，且基数也可能不同，不能凭 +${r}% 和 -${r}% 判断合计不变。`
    );
  });

  const q5=Q('da_base_threshold_by_denominator','基期阈值看分母',['技巧'],()=>{
    const now=n(1200,1600); const r=pk([30,33,36]);
    const ans='小于现期，且可能低于1000要看分母是否超过约1.2—1.6';
    return R(
      `现期 ${now}，同比增长 ${r}%。判断上年是否不到1000时，第一眼看什么？`,
      ans,
      op(ans,['一定大于现期','直接把增长率加到现期上','只看现期尾数']),
      '正增长倒推基期，分母是 1+r，基期小于现期。',
      `基期=${now}÷1.${r}，先用分母大小判断是否跌破阈值。原课“不到1000家”就是用分母大于1来快速判断。`
    );
  });

  const q6=Q('da_share_difference_times_total','占比差乘总数',['技巧'],()=>{
    const total=pk([1000,1500,2000,3000]); const p1=n(45,65); const p2=n(20,40);
    const diff=p1-p2; const ans=String(Math.round(total*diff/100));
    return R(
      `总人数 ${total}，A 占 ${p1}%，B 占 ${p2}%。问 A 比 B 多多少人，应怎样算？`,
      ans,
      op(ans,[String(diff),String(total*p1/100),String(total*p2/100)]),
      '先求占比差，再乘总人数。',
      `占比差=${p1}%-${p2}%=${diff}%，人数差=${total}×${diff}%=${ans}。原课火车与飞机人数差就是这个模型。`
    );
  });

  const q7=Q('da_first_segment_not_material_whole','定位不是通读全篇',['运用'],()=>R(
    '资料分析定位时，是否每题都必须从材料第一字读到最后一字？',
    '不需要，先用题干关键词定位，但对象不匹配时继续往下找',
    op('不需要，先用题干关键词定位，但对象不匹配时继续往下找',['必须逐字通读完整材料','只看第一句话即可','只看图表标题即可']),
    '课程强调效率：先看问法、时间、对象，再找关键词；但不能错过后文限定。',
    '第一题常在第一段只是经验，真正动作是关键词定位。若前面出现“固定资产投资”但没有“东北地区”，就要继续找东北地区数据。'
  ));


  const time=C('da_round1_time_edges','时间与定位',['基础'],[q1,q7],'all');
  const avg=C('da_round1_average_edges','平均与比例',['技巧'],[q2,q3,q6],'all');
  const base=C('da_round1_base_edges','基期与合计',['技巧'],[q4,q5],'all');
  add(C('da_audit_round1_card','时间口径｜平均与比例细节',['运用'],[time,avg,base],'all'));
})();
