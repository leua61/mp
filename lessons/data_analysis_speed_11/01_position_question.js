(function(){
  const {pk,n,op,R,Q,C,add}=EX;

  const q1=Q('da_read_order','三步定位',['基础'],()=>R(
    '资料分析拿到题，最省力的首要顺序是什么？',
    '时间点→对象→问法',
    op('时间点→对象→问法',['对象→选项→公式','先列式→再找材料','只看关键词','先看选项均衡']),
    '先判断现期、基期或隔年，再确认对象完整限定，最后看问什么。',
    '课程反复强调先看时间点，再看对象，再看问法；这能避免拿错年份、对象和分母。'
  ));

  const q2=Q('da_time_identity','时间身份',['基础'],()=>{
    const y=n(2014,2024);
    const ask=pk([y,y-1,y-2]);
    const ans=ask===y?'现期':(ask===y-1?'基期':'隔年基期');
    return R(
      `材料给的是 ${y} 年数据，题目问 ${ask} 年。这个题首先应识别为？`,
      ans,
      op(ans,['现期','基期','隔年基期','平均数升降']),
      '材料年份和问题年份相同是现期，差一年是基期，差两年是隔年。',
      `材料 ${y} 年，问题 ${ask} 年，所以是${ans}问题。`
    );
  });

  const q3=Q('da_object_limit','对象限定',['基础'],()=>{
    const cases=[
      ['东北地区民间固定资产投资','固定资产投资','民间固定资产投资','全国固定资产投资'],
      ['非养殖水产品产量','养殖水产品产量','水产品出口额','水产品进口量'],
      ['批发和零售业直接投资金额','直接投资总额','新设立企业数','房地产直接投资额']
    ];
    const c=pk(cases);
    return R(
      `题干完整对象是“${c[0]}”。找材料时最不能只抓哪个残缺对象？`,
      c[1],
      op(c[1],c.slice(2).concat([c[0]])),
      '对象限定词不能丢，尤其地区、民间、非、行业、金额/数量。',
      `“${c[1]}”少了关键限定，容易把全国或其他口径的数据拿来误用。`
    );
  });

  const q4=Q('da_ask_type','问法翻译',['基础'],()=>{
    const item=pk([
      ['增幅为多少','增长率'],
      ['增加了多少','增长量'],
      ['平均每家','平均数'],
      ['占比为多少','比重'],
      ['贸易状态','顺差或逆差']
    ]);
    return R(
      `题目问“${item[0]}”，本质上先归类为？`,
      item[1],
      op(item[1],['增长率','增长量','平均数','比重','顺差或逆差']),
      '先把中文问法翻译成模型。',
      `“${item[0]}”对应${item[1]}，之后才选择公式或估算方法。`
    );
  });

  add(C('da_reading_card','审题定位｜时间对象问法',['基础'],[q1,q2,q3,q4],'all'));
})();
