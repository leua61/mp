(function(){
  const {pk,n,op,R,Q,C,add}=EX;

  const s1=Q('da_trade_balance','顺差逆差',['基础'],()=>{
    const exp=n(60,150);
    const imp=n(20,120);
    const ans=exp>imp?'顺差':'逆差';
    return R(
      `出口额 ${exp}，进口额 ${imp}。贸易状态是什么？`,
      ans,
      op(ans,['顺差','逆差','无法判断','增长率']),
      '顺差看出口额大于进口额，逆差相反。',
      `${exp}${exp>imp?'>':'<'}${imp}，所以是${ans}。注意看“额”，不是看“量”。`
    );
  });

  const s2=Q('da_fast_than_whole','快于整体',['基础'],()=>{
    const whole=n(5,12);
    const x=whole+n(1,10);
    const y=whole-n(1,4);
    return R(
      `整体增速 ${whole}%。甲行业 ${x}%，乙行业 ${y}%。谁快于整体？`,
      '甲行业',
      op('甲行业',['乙行业','两者都快','两者都不快']),
      '快于整体就是增长率大于整体。',
      `${x}%>${whole}%，所以甲行业快于整体；${y}%<${whole}%，乙不快于整体。`
    );
  });

  const s3=Q('da_decline_faster','降幅快于整体',['基础'],()=>{
    const whole=n(6,12);
    const more=whole+n(2,10);
    const less=whole-n(1,4);
    return R(
      `整体下降 ${whole}%。甲下降 ${more}%，乙下降 ${less}%。谁的降幅快于整体？`,
      '甲',
      op('甲',['乙','两者都是','两者都不是']),
      '降幅快于整体，就是下降绝对值更大。',
      `${more}% 的下降幅度大于 ${whole}%，所以甲快于整体。`
    );
  });

  const s4=Q('da_option_balance','综合题策略',['运用'],()=>R(
    '综合判断题没时间时可以参考选项均衡，但有时间应怎样做？',
    '优先验证容易判断的选项',
    op('优先验证容易判断的选项',['完全只按均衡猜','从 A 到 D 全部精算','先忽略题干']),
    '选项均衡只是应急，不是定理。',
    '课程中多次强调：没时间可猜，有时间要验证，尤其 A、C、D 等可疑或易判选项。'
  ));

  const s5=Q('da_same_diff_option','同存异',['运用'],()=>R(
    '组合选项中，多个选项共同出现的元素与唯一缺失的元素，通常可以怎么用？',
    '先用同存异排除或优先验证',
    op('先用同存异排除或优先验证',['直接全部相加','只看第一个词','一定选唯一项']),
    '同存异是省时间的选项意识。',
    '多个选项都有的内容更可能是稳定项；少数独有或缺失项适合先查验，但不能当作必然规律。'
  ));

  add(C('da_comprehensive_card','综合判断｜贸易比较与选项意识',['运用'],[s1,s2,s3,s4,s5],'all'));
})();
