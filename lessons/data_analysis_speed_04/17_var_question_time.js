(function(){
  const {n,R,Q,C,add}=EX;
  const early=Q('var_time_early','问题早于材料',['基础'],()=>{
    const mat=n(2018,2024); const ask=mat-n(1,4);
    return R(`材料给${mat}年，问题问${ask}年数据，通常先判断为哪类？`,'基期问题',['基期问题','现期问题','未来预测','百分点问题'],'问题时间更早。',`${ask}早于${mat}，通常是从材料时间倒推基期。`);
  });
  const same=Q('var_time_same','问题等于材料',['基础'],()=>{
    const y=n(2018,2024);
    return R(`材料和问题都问${y}年，通常优先按什么处理？`,'现期或当期增长相关',['现期或当期增长相关','基期倒推','未来预测','无关年份'],'同年不倒推。','问题时间等于材料时间，通常围绕现期量或当期增长关系。');
  });
  const late=Q('var_time_late','问题晚于材料',['基础'],()=>{
    const mat=n(2016,2021); const ask=mat+n(1,4);
    return R(`材料给${mat}年，问题问${ask}年，通常是什么方向？`,'未来现期问题',['未来现期问题','基期问题','年均增长量','差分法'],'问题时间更晚。',`${ask}晚于${mat}，通常按增长率往后推未来量。`);
  });
  const timeRoute=C('var_time_direction','时间方向判断',['基础'],[early,same,late],'sequence');

  const pct=Q('var_unit_percent','百分号选项',['基础'],()=>R('四个选项分别为12.5%、36亿元、54万人、120吨，题目大概率问什么？','增长率',['增长率','增长量','具体人数','具体产量'],'百分号对应率。','带百分号的答案形态通常对应增长率、增速或增幅。'));
  const real=Q('var_unit_real','实际单位选项',['基础'],()=>R('选项都是“亿元”，题干问“增长多少”，应优先判断为？','增长量',['增长量','增长率','增速','百分点'],'实际单位对应量。','“增长多少亿元”问增长量。'));
  const speed=Q('var_unit_speed','增长快慢问法',['基础'],()=>R('“下列哪项增长最快”通常比较什么？','增长率',['增长率','增长量','现期量','基期量'],'快慢看速度。','增长最快、增速最高、增长幅度最大，本质是比较增长率。'));
  const unitRoute=C('var_unit_route','单位与措辞判断',['基础'],[pct,real,speed],'all');

  const month=Q('var_ring_month','月份环比',['基础'],()=>{
    const m=n(2,12);
    return R(`${m}月环比增长，是与哪个月比较？`,`${m-1}月`,[`${m-1}月`,`${m}月`,`${m+1>12?1:m+1}月`,`去年${m}月`],'环比看紧邻上一期。',`${m}月环比是和${m-1}月比较。`);
  });
  const quarter=Q('var_ring_quarter','季度环比',['基础'],()=>{
    const q=n(2,4);
    return R(`${q}季度环比，是与哪个季度比较？`,`${q-1}季度`,[`${q-1}季度`,`${q}季度`,`${q+1>4?1:q+1}季度`,`去年${q}季度`],'季度环比看上一季度。',`${q}季度的上一季度是${q-1}季度。`);
  });
  const cross=Q('var_ring_cross_year','跨年上一季度',['基础'],()=>{
    const y=n(2016,2025);
    return R(`${y}年一季度环比，是与哪个时期比较？`,`${y-1}年四季度`,[`${y-1}年四季度`,`${y}年二季度`,`${y-1}年一季度`,`${y}年全年`],'一季度往前跨年。',`${y}年一季度的上一季度是${y-1}年四季度。`);
  });

  const yoy=Q('var_ring_yoy','同比训练',['基础'],()=>{
    const y=n(2018,2025); const m=n(1,12);
    return R(`${y}年${m}月同比增长，是与哪个时期比较？`,`${y-1}年${m}月`,[`${y-1}年${m}月`,`${y}年${m===1?12:m-1}月`,`${y}年${m}月`,`${y-1}年全年`],'同比看去年同期。',`同比不是紧邻上月，而是和上一年同一时期比，所以是${y-1}年${m}月。`);
  });
  const ringRoute=C('var_ring_route','环比/同比训练',['基础'],[month,quarter,cross,yoy],'sequence');

  add(C('var_question_time','变量训练｜问法识别与环比',['基础'],[timeRoute,unitRoute,ringRoute],'all'));
})();
