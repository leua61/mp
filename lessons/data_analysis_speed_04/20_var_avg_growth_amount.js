(function(){
  const {n,R,Q,C,add,choose}=EX;
  const normal=Q('var_aga_normal','2015到2018除以3',['技巧'],()=>{
    const start=n(100,400), end=start+n(60,180); const ans=Math.round((end-start)/3);
    return R(`2015年为${start}亿元，2018年为${end}亿元，年均增长量约为多少？`,`${ans}亿元`,[`${ans}亿元`,`${Math.round((end-start)/4)}亿元`,`${end-start}亿元`,`${Math.round(end/3)}亿元`],'年份差=2018-2015=3。',`年均增长量=(${end}-${start})/3≈${ans}亿元。`);
  });
  const notCount=Q('var_aga_not_count','年份差不是年份个数',['技巧'],()=>R('2015、2016、2017、2018共4个年份，为什么年均增长量分母不是4？','增长间隔只有3个',['增长间隔只有3个','因为2018不计算','因为2015不计算','因为要除以5'],'看间隔，不看年份个数。','从2015到2018经历2015→2016、2016→2017、2017→2018，共3个增长间隔。'));
  const gap=C('var_aga_gap','普通年份差',['技巧'],[normal,notCount],'sequence');

  const hasPrev=Q('var_aga_has_prev','材料有前一年',['运用'],()=>{
    const y=n(2015,2020); const prev=y-1; const p=n(80,200), end=p+n(80,240); const ans=Math.round((end-p)/(y+3-prev));
    return R(`题问${y}—${y+3}平均每年增长，材料给${prev}年${p}亿元和${y+3}年${end}亿元。按课程规则初期取哪年？`,`${prev}年`,[`${prev}年`,`${y}年`,`${y+3}年`,`${y+1}年`],'有前一年，用前一年作初期。',`按老师规则，若材料给${prev}年，则把${y}看成第一年增长后的结果，初期取${prev}年。`);
  });
  const noPrev=Q('var_aga_no_prev','材料无前一年',['运用'],()=>R('题问2016—2019平均每年增长，但材料没有2015年数据，应取哪年为初期？','2016年',['2016年','2015年','2019年','2017年'],'没有前一年只能用首年。','材料没有前一年数据时，用题干首年2016作为初期。'));
  const firstGrowth=Q('var_aga_first_growth','题干首年与增长首年',['运用'],()=>R('“2015—2018平均每年增长”且给2014数据时，为什么可能用2014作初期？','把2015视作第一年增长后的结果',['把2015视作第一年增长后的结果','因为年份差必须是4','因为2015没有意义','因为2018不能作末期'],'这是课程规则的陷阱。','若材料给2014，课程规则把2015视为第一年增长后的结果，因此用2014到2018计算。'));
  const prevTrap=C('var_aga_prevtrap','前一年陷阱',['运用'],[hasPrev,noPrev,firstGrowth],'sequence');

  const yuan=Q('var_aga_unit_yuan','亿元',['基础'],()=>R('选项单位是亿元，且问平均每年增长多少，判断为？','年均增长量',['年均增长量','年均增长率','百分点','增速'],'亿元是实际单位。','带亿元、万元、吨等实际单位，是量，不是率。'));
  const wan=Q('var_aga_unit_wan','万人',['基础'],()=>R('选项单位是万人，且问平均每年增加多少，判断为？','年均增长量',['年均增长量','年均增长率','百分比','环比增速'],'万人是实际单位。','实际单位对应年均增长量。'));
  const notPct=Q('var_aga_unit_not_pct','百分号不是年均增长量',['基础'],()=>R('求年均增长量时，哪个选项形态最可疑？','12.5%',['12.5%','12.5亿元','12.5万人','12.5万吨'],'百分号是率。','年均增长量应带实际单位，百分号通常是年均增长率。'));
  const unit=C('var_aga_unit','单位识别',['基础'],[yuan,wan,notPct],'all');

  add(C('var_avg_growth_amount','变量训练｜年均增长量',['技巧'],[gap,prevTrap,unit],'all'));
})();
