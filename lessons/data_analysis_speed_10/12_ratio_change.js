(function(){
  const {n,op,R,Q,C,add}=EX;
  const qs=[
    Q('ratio_dir_up','方向上升',['基础'],()=>R(
      '部分增长率大于整体增长率时，比重如何变化？','上升',
      ['上升','下降','不变','无法判断'],
      '部分跑得比整体快。',
      'r部 > r整，则部分占整体的比重上升。'
    )),
    Q('ratio_dir_down','方向下降',['基础'],()=>R(
      '部分增长率小于整体增长率时，比重如何变化？','下降',
      ['下降','上升','不变','一定为负'],
      '部分跑得比整体慢。',
      'r部 < r整，则比重下降。'
    )),
    Q('ratio_change_formula','变化量公式',['技巧'],()=>R(
      '比重变化量的结构是：现期比重 × 什么？','(r部-r整)/(1+r部)',
      ['(r部-r整)/(1+r部)','(r整-r部)/(1+r整)','r部+r整','现期/基期'],
      '先看部分增速和整体增速之差。',
      '比重变化量 = 现期比重 × (r部-r整)/(1+r部)。'
    )),
    Q('ratio_change_sign_var','先判方向',['技巧'],()=>{
      const rp=n(3,8), rt=n(9,14); return R(
        `部分增长率 ${rp}%，整体增长率 ${rt}%。比重变化方向是？`, '下降',
        ['下降','上升','不变','无法判断'],
        '比较 r部 与 r整。',
        `${rp}% < ${rt}%，所以比重下降。`
      );
    }),
    Q('ratio_small_guess','幅度经验',['运用'],()=>R(
      '比重变化题先判定“下降”，剩余两个幅度选项很接近时，课程经验通常优先考虑什么？','较小幅度',
      ['较小幅度','较大幅度','字母最靠前','单位最长'],
      '这是经验，不是公式。',
      '方向靠 r部-r整 判断；幅度接近时可结合公式估算，经验上常优先小幅。'
    ))
  ];
  add(C('ratio_change','比重变化｜方向与公式',['技巧'],qs,'all'));
})();
