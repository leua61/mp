(function(){
  const {n,op,R,Q,C,add,pk}=EX;

  const table=C('scope_random_table','口径变量训练',['技巧'],[
    Q('scope_money_vs_item_var','钱与件不混分母',['技巧'],()=>{
      const money=n(420,980), goods=n(12,90), other=n(80,260);
      const ans=`${money}/(${money}+${other})`;
      return R(
        `某年社会捐赠款 ${money} 亿元，另有捐赠物资 ${goods} 万件、其他捐赠款 ${other} 亿元。求“捐赠款占总捐赠款比重”，正确分母应怎样列？`, ans,
        [ans,`${money}/(${money}+${goods}+${other})`,`${goods}/(${money}+${goods})`,`${money}/${goods}`],
        '先看单位和对象口径，钱不能和件相加。',
        `总捐赠款只包含钱的口径：${money}+${other}。${goods} 万件不是款项，不能混入分母。`
      );
    }),
    Q('scope_header_var','表头指代变量',['基础'],()=>{
      const header=pk(['税收收入','直接费用','接收申请项目数','限额以上零售额']);
      return R(
        `材料表头写的是“${header}”。做题取数时，第一步应确认什么？`, '表头对应的对象和单位',
        ['表头对应的对象和单位','选项字母顺序','材料页数','题号奇偶'],
        '表头决定这一列到底是什么。',
        `表格题不能只看数字，要先看“${header}”这一列代表的对象和单位。`
      );
    })
  ],'all');

  const locate=C('scope_random_locate','定位变量训练',['基础'],[
    Q('read_order_variable','题序材料顺序变量',['基础'],()=>{
      const qno=n(1,5); const ans=qno<=2?'优先从材料前部找':'可从材料相对后部快速找';
      return R(
        `一篇资料分析共 5 小题，现在做第 ${qno} 题。按课程经验，定位数据时更节能的起点是？`, ans,
        op(ans,['完全随机找','只看最后一段','先精读全文']),
        '题序通常和材料顺序大体对应，但不是绝对。',
        `第 ${qno} 题通常对应材料${qno<=2?'前部':'相对后部'}，先快读扫关键词，扫到后再精读。`
      );
    }),
    Q('read_keyword_bridge_var','关键词桥接变量',['运用'],()=>{
      const obj=pk(['青年科学基金','地区科学基金','重点项目','网络零售']);
      return R(
        `题干对象是“${obj}”，材料先出现了相关上位词，后面才出现具体对象。最稳的处理是什么？`, '把关键词连接起来再精读',
        ['把关键词连接起来再精读','只读第一句话就选','找不到完全同词就放弃','直接套任意公式'],
        '原课强调找到关键词后要精读，并连接对象。',
        `先快扫“${obj}”或相关词，发现相关信息后进入精读，把上位词、对象、率或量连接起来。`
      );
    })
  ],'all');

  add(C('scope_random_enrichment','读题口径升华｜变量定位',['技巧'],[table,locate],'all'));
})();
