(function(){
  const {n,op,R,Q,C,add}=EX;
  const qs=[
    Q('special_two_year_formula','隔年公式',['技巧'],()=>{
      const r1=n(5,18), r2=n(8,22); const ans=`${r1}%+${r2}%+${r1}%×${r2}%`;
      return R(
        `若 2014 年增长率为 ${r1}%，2015 年增长率为 ${r2}%，2015 相对 2013 的隔年增长率结构是什么？`, ans,
        op(ans,[`${r1}%+${r2}%`,`${r2}%-${r1}%`,`${r1}%×${r2}%`]),
        '隔年是连续两次增长。',
        '隔年增长率 = r1 + r2 + r1×r2。'
      );
    }),
    Q('special_product_same','乘积同形',['基础'],()=>R(
      '隔年增长率公式和哪类增长率公式同形？','乘积增长率',
      ['乘积增长率','平均数增长率','百分点差','简单加法'],
      '连续变化本质是乘法展开。',
      '(1+r1)(1+r2)-1 = r1+r2+r1r2。'
    )),
    Q('mixed_between','混合夹逼',['技巧'],()=>R(
      '两个部分增长率分别为 8% 和 12%，整体混合增长率一定在哪里？','8% 到 12% 之间',
      ['8% 到 12% 之间','小于 8%','大于 12%','一定等于 10%'],
      '整体由部分混合，不会越界。',
      '混合增长率夹在各部分增长率之间。'
    )),
    Q('mixed_weight','偏向量大者',['技巧'],()=>R(
      'A 部分增长率 12%，量很大；B 部分增长率 8%，量较小。混合增长率更偏向谁？','12%',
      ['12%','8%','0%','二者之外'],
      '量大者权重更大。',
      '混合增长率不只看增长率大小，还看对应量的权重。'
    )),
    Q('mixed_three','三者混合',['技巧'],()=>R(
      '三部分混合增长率，最省力的处理方式通常是什么？','先混两个，再与第三个混',
      ['先混两个，再与第三个混','三个全部精算','只看最大增长率','只看最小增长率'],
      '分步夹逼更快。',
      '先合并两个部分，判断混合值区间和偏向，再与第三者继续混合。'
    ))
  ];
  add(C('special_growth','特殊增长率｜隔年与混合',['技巧'],qs,'all'));
})();
