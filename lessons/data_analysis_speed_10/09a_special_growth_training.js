(function(){
  const {n,op,R,Q,C,add}=EX;

  const twoYear=C('special_two_year_training','隔年变量训练',['技巧'],[
    Q('two_year_formula_var','隔年列式',['技巧'],()=>{
      const r1=n(5,14), r2=n(6,18); const ans=`${r1}%+${r2}%+${r1}%×${r2}%`;
      return R(
        `某指标 2016 年同比增长 ${r1}%，2015 年同比增长 ${r2}%。2016 年相对 2014 年增长率列式应为？`, ans,
        [ans,`${r1}%+${r2}%`,`${r1}%-${r2}%`,`${r1}%×${r2}%`],
        '隔一年要把两个增长过程连乘。',
        '隔年增长率 = r₁+r₂+r₁r₂；不能只把两个百分数相加。'
      );
    }),
    Q('two_year_lower_bound_var','隔年下界',['技巧'],()=>{
      const r1=n(6,12), r2=n(18,32); const sum=r1+r2;
      return R(
        `两个同比增速分别为 ${r1}% 和 ${r2}%。隔年增长率一定比 ${sum}% 怎样？`, '更大',
        ['更大','更小','相等','无法判断'],
        '两个正增长率相乘项为正。',
        `隔年增长率=${r1}%+${r2}%+${r1}%×${r2}%，所以大于 ${sum}%。`
      );
    })
  ],'all');

  const mixed=C('mixed_growth_training','混合变量训练',['技巧'],[
    Q('mixed_name_trap','平均增长率名称陷阱',['基础'],()=>R(
      '多个地区合在一起求总体增速，课程中应按什么本质处理？','混合增长率',
      ['混合增长率','时间年均增长率','百分点变化','间隔做差'],
      '不要被“平均增长率”口头说法带偏。',
      '多对象合并后的总体增速，本质是混合增长率：夹在各部分增速之间，并偏向基期量/权重大的一方。'
    )),
    Q('mixed_between_var','混合夹逼变量',['技巧'],()=>{
      const a=n(5,9), b=n(11,16); const ans=`在 ${a}% 和 ${b}% 之间`;
      return R(
        `甲增长 ${a}%，乙增长 ${b}%。甲乙合计增长率应在哪里？`, ans,
        op(ans,[`小于 ${a}%`,`大于 ${b}%`,`等于 ${(a+b)*2}%`]),
        '总体增速夹在部分增速之间。',
        `混合增长率不会跳出 ${a}%~${b}% 的范围。`
      );
    }),
    Q('mixed_weight_var','偏向量大者变量',['技巧'],()=>{
      const low=n(5,8), high=n(12,16), big=EX.pk(['低增速一方','高增速一方']);
      const ans=big==='高增速一方' ? '偏向高增速' : '偏向低增速';
      return R(
        `甲增长 ${low}%，乙增长 ${high}%。若${big}的基期量明显更大，合计增速会怎样？`, ans,
        op(ans,['居中且完全平均','一定等于低增速','一定等于高增速']),
        '谁的量大，谁的拉力大。',
        `混合增速仍在 ${low}% 与 ${high}% 之间，但会${ans}。`
      );
    }),
    Q('mixed_three_pair_var','三者先混两个',['运用'],()=>R(
      '三部分混合增长率不好直接看时，课程建议先怎样处理？','先混其中两个',
      ['先混其中两个','三个百分数直接平均','只看最高增长率','只看最低增长率'],
      '三者混合可拆成两步。',
      '先把两个部分混成一个总体，再与第三个部分继续混合，仍使用“夹逼、偏向量大者”。'
    ))
  ],'all');

  add(C('special_growth_training','特殊增长率变量训练｜隔年与混合',['技巧'],[twoYear,mixed],'all'));
})();
