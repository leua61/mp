(function(){
  const {op,R,Q,C,add}=EX;

  const frac=C('fraction_error_memory','常见分数与误差回补',['技巧'],[
    Q('speed_four_seven_memory','4/7 敏感度',['基础'],()=>R(
      '4/7 约等于多少？','57.1%',
      ['57.1%','42.9%','46.7%','66.7%'],
      '7 的循环小数要熟一点。',
      '4/7≈0.571428，即约 57.1%。原课用它提醒：划线估算后别忘了误差。'
    )),
    Q('speed_seven_family_var','七分数随机敏感度',['技巧'],()=>{
      const pairs=[['1/7','14.3%'],['2/7','28.6%'],['3/7','42.9%'],['4/7','57.1%'],['5/7','71.4%'],['6/7','85.7%']];
      const [f,p]=EX.pk(pairs);
      return R(
        `${f} 最接近多少？`, p,
        op(p,pairs.map(x=>x[1]).filter(x=>x!==p)),
        '七分数是循环小数，资料估算中常用于回补误差。',
        `${f}≈${p}。不必精算很多位，但要知道大致百分数。`
      );
    }),
    Q('speed_error_two_ratios','两个比值都近似',['运用'],()=>R(
      '比较两个增长率差时，如果两个“现期÷基期”都被近似过，最容易丢掉什么？','两个近似误差的方向',
      ['两个近似误差的方向','题干年份','选项字母顺序','单位名称'],
      '一个比值的误差可能会被另一个比值抵消或放大。',
      '增长率差不是单个比值；两个比值都估算时，要看各自偏大偏小，选项近时必须回补。'
    ))
  ],'all');

  add(C('fraction_error_extra','速算补全｜七分数与双比值误差',['技巧'],[frac],'all'));
})();
