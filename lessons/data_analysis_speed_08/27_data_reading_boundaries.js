(function(){
  const {R,Q,C,add,n,op}=EX;
  const subs=[
    Q('time_current_base','时间定位',['基础'],()=>R(
      '材料给2017年数据，题目问2016年，通常先判断为哪类问题？',
      '基期问题',
      ['现期问题','基期问题','翻番问题','贡献率问题'],
      '题目年份早于材料年份。',
      '材料年=题目年通常按现期处理；题目问上一年或更早年份，通常要由现期倒推基期。'
    )),
    Q('increase_decrease_back','增减量倒推',['技巧'],()=>{
      const base=n(80,180), inc=n(5,30), cur=base+inc;
      return R(`某地现期出生人口${cur}，比上年增加${inc}。上年是多少？`,String(base),op(base,[cur,cur+inc,base-inc]),'增加多少，就从现期中减回去。',`${cur}-${inc}=${base}。若题干说“减少x”，则基期=现期+x。`);
    }),
    Q('higher_lower_point','高于低于百分点',['基础'],()=>R(
      '浙江增速5%，高于全国2.8个百分点，则全国增速是多少？',
      '2.2%',
      ['2.2%','7.8%','5%','2.8%'],
      '“高于”说明浙江=全国+2.8个百分点。',
      '全国增速=5%-2.8%=2.2%。若A低于B x个百分点，则B=A+x个百分点。'
    )),
    Q('given_share_add','表头占比直接加',['技巧'],()=>R(
      '表格两列已经给出“金融证券类占比12.4%”“支付交易类占比87.3%”，求二者合计占比，应怎么做？',
      '直接相加',
      ['直接相加','先除以总量','先求增长率','先转成基期量'],
      '表头已经说明数据是占比。',
      '二者占同一个总量的比重，合计占比=12.4%+87.3%=99.7%，不必再除以总量。'
    )),
    Q('given_current_share','现期比重已给',['技巧'],()=>R(
      '若题目已直接给出现期比重41%，求“较上年提高多少”，现期部分应如何处理？',
      '直接用41%减基期比重',
      ['直接用41%减基期比重','重新用现期部分量除整体量','用41%乘增长率','改算贡献率'],
      '已给的数据不要重复计算。',
      '比重变化=现期比重-基期比重；现期比重已给时，重点求基期比重即可。'
    )),
    Q('read_question_max_min','问法审题',['基础'],()=>R(
      '题干问“最大是多少”，审题上最不能犯的错误是什么？',
      '把最大看成最小',
      ['把最大看成最小','先找分母','先看单位','先写公式'],
      '原文开头专门提醒过审题方向。',
      '方法再快也要先看清问法：最大、最小、多、少、上升、下降，方向错了就不是计算问题。'
    )),
    Q('same_year_current','材料同年现期',['基础'],()=>R(
      '材料和题目都指向同一年，例如材料是2010年、题目也问2010年，通常按什么量处理？',
      '现期量',
      ['现期量','基期量','增长量','番数'],
      '年份一致时通常不需要倒推。',
      '题问年份与材料年份一致，先按现期量找数；题问上年或过去年份，才考虑基期倒推。'
    ))
  ];
  add(C('data_reading_boundaries_card','读题定位｜时间、表头与百分点',['基础'],subs,'all'));
})();
