(function(){
  const {n,pct,op,R,Q,C,add}=EX;
  const chain=C('share_chain_route','相邻年份升降链',['技巧'],[
    Q('share_next_year','后年更大吗',['技巧'],()=>{
      const rp=n(6,20), rw=rp-n(1,5); const ans='后一年比重大';
      return R(`某年部分增长${pct(rp)}，整体增长${pct(rw)}。与上一年相比，该年比重如何？`,ans,op(ans,['后一年比重大','后一年比重小','无法判断','一定不变']), '比较R部和R整。',`R部=${pct(rp)}>R整=${pct(rw)}，比重上升。`);
    }),
    Q('share_candidate','锁定候选',['运用'],()=>{
      const ans='只保留升降链最终峰值候选';
      return R('连续多年比重最大值题，若能通过R部与R整判断每年相对上一年升降，第一步应做什么？',ans,op(ans,['每年比重全算','只保留升降链最终峰值候选','直接选最后一年','直接选第一年']), '最大最小题可以先用趋势排除。',`先判断相邻年份比重升降，排除不可能成为最大值的年份，再对候选精算。`);
    })
  ],'sequence');
  const calc=C('share_candidate_calc','先排除再计算',['运用'],[
    Q('two_segments','两段趋势',['运用'],()=>{
      const ans='两个局部峰值';
      return R('若前半段比重先降后升，后半段连续上升，通常最后需要比较哪些年份？',ans,op(ans,['全部年份','两个局部峰值','只比第一年和第二年','只看增长率最大年']), '趋势链只能排除，不一定直接给最终答案。',`每段趋势留下局部峰值，最后比较这些候选即可。`);
    }),
    Q('candidate_only','只算候选',['技巧'],()=>{
      const a=n(3000,8000), b=n(9000,18000); const ans='只计算候选年份比重';
      return R(`已排除大部分年份，只剩两年候选。接下来应如何求最大比重？`,ans,op(ans,['从头全算','只计算候选年份比重','继续只看标题','改算贡献率']), '排除后不要返工。',`趋势判断只是锁定候选，最终需要对候选的部分/整体进行比较或估算。`);
    })
  ],'sequence');
  add(C('share_max_training_card','比重最大最小｜变量训练',['运用'],[chain,calc],'all'));
})();
