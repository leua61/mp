      ['增长量是绝对量，比重做差仍是百分点/百分数差','因为比重不能做差','因为比重一定做比','因为增长量必须是负数'],
      '注意“变化”和“增长量”的命名边界。',
      '平均数是绝对量，做差可叫平均数增长量；比重是相对量，做差结果叫比重变化或百分点变化。'
