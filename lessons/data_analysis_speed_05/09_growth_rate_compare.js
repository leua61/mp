(function(){
  const {n,op,R,Q,C,add,d}=EX;
  const qs=[
    Q('compare_true_formula','正规公式',['基础'],()=>R('增长率的正规概念是：','增长量/基期',['增长量/基期','增长量/现期','现期/基期','基期/现期'],'定义看分母。','增长率=增长量÷基期。')),
    Q('compare_current_possible','现期比较可行',['技巧'],()=>R('只给现期和增长量，比较增长率大小时，能否用增长量/现期比较？','可以',['可以','绝对不可以','只能比较增长量','只能比较现期'],'比较方向不变。','增长量/现期越大，增长率也越大，可以用来比大小。')),
    Q('compare_derivation','推导关键',['技巧'],()=>R('增长量/现期可比的关键变形是：','基期=现期-增长量',['基期=现期-增长量','现期=基期-增长量','增长量=基期-现期','百分点=百分数-1'],'先把基期写出来。','基期=现期-增长量，进而可证明增长量/现期与增长率同向。')),
    Q('compare_inverse','倒数方向',['技巧'],()=>R('现期/增长量越大，增长率通常：','越小',['越大','越小','不变','无法比较'],'它是增长量/现期的倒数。','现期/增长量越大，增长量/现期越小，增长率也越小。')),
    Q('compare_variable','变量比较',['运用'],()=>{
      const inc1=n(20,80), cur1=n(200,600), inc2=n(20,80), cur2=n(200,600);
      const v1=inc1/cur1, v2=inc2/cur2;
      const ans=v1>v2?'甲':'乙';
      return R(`甲：增长量 ${inc1}、现期 ${cur1}；乙：增长量 ${inc2}、现期 ${cur2}。谁的增长率更大？`,ans,op(ans,['甲','乙','一样大','无法判断']),'比较增长量/现期即可。',`甲增长量/现期≈${d(v1,3)}，乙≈${d(v2,3)}，所以${ans}更大。`);
    }),
    Q('compare_not_must','方法边界',['基础'],()=>R('用增长量/现期比较增长率的态度应是：','可以用，但不一定总是最省事',['必须永远用','绝对不能用','可以用，但不一定总是最省事','只适用于负增长'],'课程说“可以，但没必要迷信”。','有时直接用现期-增长量求基期再比，会更直观。'))
  ];
  add(C('growth_compare','增长率比较｜现期分母法',['技巧'],qs,'all'));
})();
