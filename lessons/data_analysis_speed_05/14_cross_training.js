(function(){
  const {n,op,R,Q,C,add}=EX;
  const trigger=Q('cross_train_trigger','问人数识别',['基础'],()=>R(
    '题目问“受访男性居民约多少人”，材料只给整体平均用时、男性平均用时、女性平均用时。应想到什么？',
    '用平均值构造十字交叉',
    ['用平均值构造十字交叉','用隔年增长率','用指数减 100','直接平均两个选项'],
    '人数是平均值的分母。',
    '平均用时=总用时/人数，所以可通过男女平均值与整体平均值构造人数比。'
  ));
  const denom=Q('cross_train_denom','分母就是人数',['基础'],()=>R('平均时间=总时间/什么？','人数',op('人数',['增长率','百分点','年份','指数']),'平均值的分母就是要找的数量。','平均时间=总时间÷人数。'));
  const route1=C('cross_people_route','人数识别',['基础'],[trigger,denom],'all');

  const minuteConvert=Q('cross_train_minutes','时间转分钟',['技巧'],()=>{
    const h=n(3,5), m=n(5,55), ans=String(h*60+m);
    return R(`${h} 小时 ${m} 分钟等于多少分钟？`,ans,op(ans,[String(h*100+m),String(h*60),String(m*60+h),String(h+m)]),'十字交叉前先统一单位。',`${h}小时=${h*60}分钟，加 ${m} 分钟，共 ${ans} 分钟。`);
  });
  const diffRatio=Q('cross_train_diff_ratio','做差得比值',['技巧'],()=>R(
    '整体平均 264 分，男性 315 分，女性 215 分。十字交叉得到男:女应看哪组差？',
    '(264-215):(315-264)',
    ['(264-215):(315-264)','(315-264):(264-215)','(315-215):(264-215)','315:215'],
    '分母比等于对侧差。',
    '男性人数:女性人数=(整体-女性):(男性-整体)=(264-215):(315-264)=49:51。'
  ));
  const totalAllocate=Q('cross_train_allocate','总人数分配',['技巧'],()=>{
    const total=n(40000,60000), a=49, b=51; const ans='略少于一半';
    return R(`若男:女=49:51，总人数 ${total}，男性人数与一半相比？`,ans,op(ans,['略少于一半','略多于一半','正好一半','等于女性人数两倍']),'49 份少于 51 份。',`男性占 49/(49+51)=49%，所以略少于一半。`);
  });
  const route2=C('cross_construct_route','构造训练',['技巧'],[minuteConvert,diffRatio,totalAllocate],'sequence');

  const maleNotUrban=Q('cross_match_male','问男性不用城乡',['基础'],()=>R(
    '题目问男性人数，但你用城镇/农村平均值构造十字交叉，会得到什么？',
    '城镇/农村人数比，不是男女人数比',
    ['城镇/农村人数比，不是男女人数比','男女人数比','增长率之比','指数之比'],
    '分组决定分母。',
    '城镇/农村平均值的分母是城乡人数，因此不能回答男性人数。'
  ));
  const urbanNotMale=Q('cross_match_urban','问城镇不用男女',['基础'],()=>R(
    '题目问城镇居民人数时，应使用哪组平均值构造？',
    '城镇/农村平均值',
    ['男性/女性平均值','城镇/农村平均值','年份指数','增长量/现期'],
    '问法决定分组。',
    '问城镇，就要用城镇与农村这组平均值。'
  ));
  const multiDistract=Q('cross_match_distract','多平均值干扰',['运用'],()=>R(
    '材料出现多组平均值时，最优先检查什么？',
    '哪组分母对应题目所问对象',
    ['哪组分母对应题目所问对象','哪组数字最大','哪组在材料最前面','哪组小数最少'],
    '十字交叉得到的是分母比。',
    '要让分母比正好对应题目所问人数、次数或个数。'
  ));
  const route3=C('cross_match_route','分组匹配',['运用'],[maleNotUrban,urbanNotMale,multiDistract],'all');
  add(C('cross_training','严格十字交叉｜变量训练',['运用'],[route1,route2,route3],'all'));
})();
